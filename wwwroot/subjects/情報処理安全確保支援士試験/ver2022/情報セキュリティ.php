上の図で理解してほしいことは、「リスク(情報セキュリティリスク)がどのように生まれるか」です。<br />リスクは<span class="underline">脆弱性 &times; 脅威</span>によって生まれます。<br /><br />言い換えれば、<span class="underline">脆弱性があっても(セキュリティが脆弱でも)脅威がなければ(誰も攻撃しなければ)リスクは発生しないし、脅威があっても(攻撃者が存在しても)脆弱性がなければ(セキュリティが強固であれば)リスクは発生しない</span>ということです。<br /><br />この世界において攻撃者がいないというのは想定し得ないので、セキュリティに注意を払わないという選択肢はないのですが、脅威がどこを向いているか(どこが狙われやすいか)をしっかりと把握することが大切です。
<h2>サイバー空間</h2>
サイバー空間とは、、、<br /><br />ITの力(コンピュータ・ネットワーク)によって創り出された仮想的な空間のことです。<br /><strong>インターネット</strong>が代表的なサイバー空間です。<br /><br />サイバー空間については、2011年に米国のクリントン国務長官が「サイバー空間の国際戦略」について公表して、サイバー空間についての基本原則を定めました。<br />この「サーバー空間の国際戦略」の内容を簡単に説明すると、<span class="underline"><strong>「自由で」「安全で」「便利な」</strong></span>サイバー空間を形成しようね♪ってことです。
