<h2>OECDセキュリティガイドライン</h2>
経済協力開発機構(OECD)が提唱した情報セキュリティに関するガイドラインです。<br />簡単に言えば、<span class="underline">システムを作る人(SE・プログラマ)もシステムを使う人(エンドユーザといいます)もセキュリティに注意を払おうね♪</span>ということです。<br /><br />OECDセキュリティガイドラインでは<strong>「セキュリティ文化」</strong>という言葉を使用しています。セキュリティ文化とは、「情報システム及びネットワークを開発する際にセキュリティに注目し、また、情報システム及びネットワークを利用し、情報をやりとりするに当たり、新しい思考及び行動の様式を取り入れること」と定義されます。
<p>リンクは<a href="https://www.ipa.go.jp/security/fy14/reports/oecd/handout.pdf">こちら</a>。</p>
<h2>情報セキュリティの重要性</h2>
IT、最近とってもブームですね♪<br />そのような社会において情報セキュリティは企業の存続を左右する状態にあります。<br />経営者・従業員全員でこのセキュリティ問題に取り組む必要があります。<br /><br />情報セキュリティを対策の基本は<strong>守るべきものを攻撃から守る</strong>です。当たり前ですね♪ですけどとっても大切なので情報セキュリティ対策について順序立てて説明していきますね♪
<h3>情報セキュリティ対策手順</h3>
<ol>
	<li><strong>何を守るべきか(<strong>情報資産</strong>)</strong>を把握する</li>
	<li>どんな攻撃が飛んでくるか(<strong>脅威</strong>)を把握する</li>
	<li>どこを狙われるか(<strong>脆弱性</strong>)を把握する</li>
	<li>情報セキュリティの方針を明確にする♪</li>
</ol>
