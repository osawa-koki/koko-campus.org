<h2>セキュリティとは</h2>
セキュリティとは、、、<br /><br />ISMS(Information Security Management System/情報セキュリティマネジメントシステム)に関する国際的標準規格であるISO/IEC27000またはその日本版であるJIS27000ファミリーでは、<strong>「情報の<span class="underline">機密性、完全性、可用性</span>を確保すること」</strong>と定義されます。<br />それぞれの頭文字を取って<strong>CIA</strong>とよく表されます。
<table>
	<caption>情報セキュリティの特性</caption>
	<tbody>
		<tr>
			<th>C</th>
			<td>機密性(Confidentiality)</td>
		</tr>
		<tr>
			<th>I</th>
			<td>完全性(Integrity)</td>
		</tr>
		<tr>
			<th>A</th>
			<td>可用性(Availability)</td>
		</tr>
	</tbody>
</table>
機密性とは、秘密にするべき情報を秘密に保持すること、<br />完全性とは、保有する情報が不正に書き換えられたり(改竄されたり)しないこと、<br />可用性とは、そのシステムが利用したいときに利用できる状態にあること、<br /><br />を指します。
<h2>セキュリティの付加的特性</h2>
ここでは、前述したセキュリティの特性(CIA)を達成するにあたり、大切となる付加的な特性について学びましょう♪<br />情報セキュリティの付加的な特性とは、<sapn class="underline">「真正性」「責任追跡性」「否認防止」「信頼性」</sapn>の4つです。
