<h2>SYN Flood攻撃</h2>
TCPの接続開始要求(SYN)を大量に送り付けます。<br />SYNを受け取ったホストはSYN/ACKを返した後にその応答であるACKをしばらく待ちますが、攻撃者はこれを無視してひたすらにSYNを送り続けます。<br /><br />SYN Flood攻撃の際にはIPアドレスを偽装したパケットが送られることが多く、攻撃者の特定は困難です。
