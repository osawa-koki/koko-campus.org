<h2>DoS攻撃</h2>
DoS攻撃とは、サービス(Service)の(of)否定(Denial)攻撃の略で、大量のデータ・パケットを送り付けてCPU・メモリやネットワーク帯域を溢れさせる攻撃です。
DoS攻撃はあくまでも大量のデータを送り付けてサービスを停止させる攻撃の総称です。
