<h2>アンプリファイア攻撃</h2>
別名、「増幅攻撃」ですが、前述したリフレクション攻撃と合わせて「反射・増幅型攻撃」と呼ばれることが多いです。<br /><br />リフレクション攻撃はなぜ攻撃者は自分で標的に直接大量のデータを送らずに、踏み台を利用して反射させて大量のデータを送ると思いますか???<br /><br /><br />答えは踏み台を利用した方が強力になるからです。<br />攻撃者のコンピュータ資源だけをフル稼働させても標的に対してサービスを停止させる程のダメージを与えることってかなり難しいんです。<br />そこで、利用できるコンピュータ資源を使っちゃえって感じです。<br /><br />アンプリファイア攻撃はリフレクション攻撃とほとんど同義です。要求パケットを反射させて応答パケットで攻撃するのがリフレクション攻撃ですが、要求パケットに対する応答パケットの方が大きい場合はその分だけより強力な増大された(アンプリファイド)攻撃をすることが可能になります。換言すれば、反射させる際にパケットが大きく増長する(増幅率が高い)場合は攻撃者自身が豊富なコンピュータ資源を有していなくても十分な攻撃が可能になります。
<img src="../pics/アンプリファイア攻撃.png" alt="アンプリファイア攻撃" />
「反射・増幅型攻撃」はその性質上、<span class="underline">コネクションを確立しないUDPであること</span>と<span class="underline">問い合わせ元を制限しない</span>機能が攻撃対象となりますが、この2つを満たす機能として「NTP」「DNS」「SSDP」「RIP」があげられます。
<div class="step">
