<h2>最小特権</h2>
情報資源にアクセスできる人・プログラムは常に<span class="underline">必要最小限の権限のみを付与</span>するという原則です。<br /><br />また、権限に期限を加えて付す際はその有効期限も必要最小限に設定するべきです。
<h2>OSのアクセス制御</h2>
OSが設定する「誰が、何に対して、何をしてよいか」のルールの仕組みは大きく以下の3つに分類されます。
<ul>
	<li>DAC</li>
	<li>MAC</li>
	<li>RBAC</li>
</ul>
以下で詳しく説明しますね♪
<h3>DAC</h3>
「Discretionary Access Control」の略で、「任意アクセス制御」と訳されます。<br /><br />一般的なOS(windows・macOS)で採用されている方式です。<br /><br />特徴としては、<span class="underline">オブジェクトの所有者がアクセス権を設定</span>することがあげられます。
