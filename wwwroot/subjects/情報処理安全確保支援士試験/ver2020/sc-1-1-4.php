	<h2>ダークウェブ</h2>
	<p>safariやgoogle chrome、InternetExplorer、yahoo!などの主要なブラウザではアクセスできないインターネット領域を指します。<br><br>一般的にダークウェブに到達するためには、<a class = "link_cancel" href = "https://www.torproject.org/">tor</a>などの特別なブラウザを使用するか、I2Pといった技術を使用する必要があります。<br><br>身元がバレずにアクセスでき、高いプライバシーが保護される反面で犯罪の温床になる点が問題視されています。</p>
	<h2>金銭奪取</h2>
	<p>2020年3月に警察庁が公表した資料によると、金銭奪取(お金を騙し取る)の代表的な例であるインターネットバンキングの不正送金事犯の被害件数・被害総額は2015年から2018年までは漸減していたのですが2019年には2018年と比べて被害件数・被害総額で6倍近くまで膨れ上がっています。<br><small>※引用元は<a href = "https://www.npa.go.jp/publications/statistics/cybersecurity/data/R01_cyber_jousei.pdf">こちら</a>。</small><br><br>近年は、金銭奪取による犯罪被害が急拡大しています。<br>有効な対策としては信頼できないwebサイトには登録しない、パスワードを複数のサイトで使いまわさない、ことがあげられます。</p>
