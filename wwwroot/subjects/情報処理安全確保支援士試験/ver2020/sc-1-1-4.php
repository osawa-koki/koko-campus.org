	<h2>サイバーテロリズム</h2>
	<p>サイバー(Cyber)×テロ(Terrorism)の2つの言葉を組み合わせた造語で、ネットワーク上で起きるテロ攻撃の総称です。「サイバー攻撃」という言葉が使われることもありますが、基本的に意味は同じです。<br>一般に「サイバー攻撃」ではなく「サイバーテロ」という言葉を用いるときは、今やITは我々の生活に必要不可欠なものでIT資産に対する攻撃はテロと同等のダメージを与えるという意味を暗示します。</p>
