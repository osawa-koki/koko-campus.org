	<h2>ISMS</h2>
	<p>ISMSとは「Infromation Security Management System」の略で、「情報セキュリティマネジメントシステム」と訳されます。<br><br>「ISO/IEC 27000」ファミリーによって標準化されています。<br><br>具体的な内容としては情報セキュリティの特性である<span class = "underline">「機密性」「完全性」「可用性」「真正性」「責任追跡性」「否認防止」「信頼性」</span>を<span class = "underline">継続的に確保</span>するために、企業が構築すべき企業体制・実施するべき運用に関するシステムのことを言います。</p>
	<p class = "explanation">
		<span class = "underline">マネジメントシステム</span>とは、、、<br><br>ISMSの「MS」の部分を言いますが、これは「JIS Q 27000」ファミリーによると、「方針・目的及びその目的を達成するためのプロセスを確立するための、相互に関連する又は相互に作用する、組織の一連の要素」と定義されます。<br><br>簡単に言えば、情報セキュリティ体制は一度完成させたらそれで終了ではなくて、常にそれを修正・改良しようね♪ってことです。<br><br>もっと簡単に言えば、ISMSとは「情報セキュリティ」×「PDCAサイクル」です。
	</p>
