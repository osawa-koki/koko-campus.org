	<h2>IPAセキュリティセンター</h2>
	<p>この試験を運営しているIPAのセキュリティセンターでは、主に<span class = "underline">「情報セキュリティ対策の強化」「セキュアな情報インフラの促進」</span>を担当しています。<br><br>また、サイバー攻撃対策に関する活動として<span class = "underline">「J-CSIP」と「J-CRAT」</span>を運営しています。</p>
	<div class = "explanation">
		<span class = "underline">J-CSIP</span>とは、、、<br><br>
		「Initiative for Cyber Security Information sharing Partnership of Japan」の略で、「サイバー情報共有イニシアティブ」とも呼ばれます。<br><br>全部で13個のSIG(類似の産業分野同士が集まったグループ)を形成してNDA(秘密保持契約)を交わしたうえでサイバー攻撃の情報を共有して、高度なサイバー攻撃対策につなげる取り組みです。<br><br>IPAのサイトでは「J-CSIPは、公的機関であるIPAを情報ハブ（集約点）の役割として、参加組織間で情報共有を行い、高度なサイバー攻撃対策に繋げていく取り組みです。」と説明されています。<br><small>※引用サイトは<a href = "https://www.ipa.go.jp/security/J-CSIP/index.html">こちら</a>。</small><br><br>
		<div class = "exam">
			&#9836;&#9836;&#9836; 令和3年春試験午前&#8545;問9 &#9836;&#9836;&#9836;
			<div class = "separator"></div>
			J-SHIPに関して、「<strong>標的型サーバー攻撃などに関する情報を参加組織間で共有し、高度なサイバー攻撃対策につなげる取り組み</strong>」と説明されています。
		</div>
	</div>
	<p class = "explanation">
		<span class = "underline">J-CRAT</span>とは、、、<br><br>
		「Cyber Rescue and Advice Team against targeted attack of Japan」の略で、「サイバーレスキュー隊」とも呼ばれます。<br><br>標的型サイバー攻撃の被害拡大防止のために、相談を受けた組織の被害の低減と攻撃の連鎖の遮断を支援する活動をする経済産業省が支援する機関です。<br><small>※引用元は<a href = "https://www.ipa.go.jp/security/J-CRAT/index.html">こちら</a>。</small>
	</p>
	<p class = "explanation">
		<span class = "underline">JPCERT/CC</span>とは、、、<br><br>
		「Japan Computer Emergency Response Team Coordination Center」の略で、「JPCERTコーディネーションセンター」とも呼ばれます。<br><br>「J-CSIP」「J-CRAT」と異なり、IPAが管理する機関ではありませんが、IPAと連携して活動することが多い機関ですので、ここで説明しますね。<br><br>JPCERT/CCとは、日本国内に関する<span class = "underline">インシデント</span>等の報告の受け付け、対応の支援、発生状況の把握、手口の分析、再発防止のための対策の検討や助言などを、技術的な立場から行う機関です。<br><small>※引用元は<a href = "https://www.jpcert.or.jp/">こちら</a>。</small>
	</p>
	<h2>JVN</h2>
	<p>「Japan Vulnerability Notes」の略で、IPAとJPCERT/CCが共同運営している、国内で使用されているソフトウェアなどの脆弱性関連情報を一元管理して提供するポータルサイトです。<br><br>以下の事項が登録されています。
