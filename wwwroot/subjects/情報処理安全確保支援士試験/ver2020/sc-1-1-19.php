	<h2>JIS Q 27014</h2>
	<p>「JIS Q 27014」では、情報セキュリティガバナンスの概念及び原則を示した規格です。<br>また、情報セキュリティガバナンスとは、コーポレート・ガバナンスと、それを支えるメカニズムである内部統制の仕組みを、情報セキュリティの観点から企業内に構築・運用することです。<br><br>情報セキュリティガバナンスの基本原則として以下の6つがあげられています。(条文5より)
		<ol class = "lili">
			<li>組織全体の情報セキュリティを確立する。</li>
			<li>リスクに基づく取組みを採用する。</li>
			<li>投資決定の方向性を設定する。</li>
			<li>内部及び外部の要求事項との適合性を確実にする。</li>
			<li>セキュリティに積極的な環境を醸成する。</li>
			<li>事業の結果に関するパフォーマンスをレビューする。</li>
		</ol>
	また、情報セキュリティガバナンスには以下の4(5)つのプロセスからなるとされます。(条文5より)
		<ol class = "lili">
			<li>評価</li>
			<li>指示</li>
			<li>モニタ</li>
			<li>コミュニケーション</li>
			<li>(保証)</li>
		</ol>
	</p>
