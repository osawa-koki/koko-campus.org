	<h2>サイバーセキュリティ基本法</h2>
	<p>サイバーセキュリティ基本法の総則第一条の一部を紹介しますね♪<br><br>「サイバーセキュリティに関する施策を総合的かつ効果的に推進し、もって経済社会の活力の向上及び持続的発展並びに国民が安全で安心して暮らせる社会の実現を図るとともに、国際社会の平和及び安全の確保並びに我が国の安全保障に寄与することを目的とする。」<br><small>※引用元は<a href = "https://elaws.e-gov.go.jp/document?lawid=426AC1000000104">こちら</a>。</small><br><br>「国際社会の平和及び安全の確保並びに我が国の安全保障に寄与」なんてすごく大ごとですよね♪<br><br>ITのすさまじい成長を鑑みると情報セキュリティの整備は今や最上級の注意が必要なことと言えます。<br><br>「ITのスーパーブーム」という時代背景から2014年11月にサイバーセキュリティ基本法が制定されました。<br>同法の施行により内閣に「<span class = "underline">サイバーセキュリティ戦略本部</span>」が、内閣官房に「<span class = "underline">内閣サーバーセキュリティセンター(NISC)</span>」が設置されました。</p>
	<h2>サイバーセキュリティ経営ガイドライン</h2>
	<p>サイバーセキュリティはITエンジニアだけの問題ではなくて、経営者が企業全体として取り組む必要がある問題であるとし、経営者が認識すべき3原則(以下)を設定しています。</p>
	<ul>
		<li>経営者は、サイバーセキュリティリスクを認識し、リーダーシップによって対策を進めることが必要</li>
		<li>自社は勿論のこと、ビジネスパートナーや委託先も含めたサプライチェーンに対するセキュリティ対策が必要</li>
		<li>平時及び緊急時のいずれにおいても、サイバーセキュリティリスクや対策に係る情報開示など、関係者との適切なコミュニケーションが必要</li>
	</ul>
	<span class = "quote">※引用元は<a href = "https://www.meti.go.jp/policy/netsecurity/downloadfiles/guide2.0.pdf">こちら</a>。</span>
