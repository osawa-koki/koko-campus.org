<h2>位置指定子</h2>
「^」と「$」の2つあります。
<h3>^</h3>
対象文字列の先頭を意味します。<br />「m」モードだと各行の行頭にマッチします。
<br /><br />
<code class="php">
	$text = preg_match("/cde/u", "abcde", $matched); //$matchedには「cde」が代入されます
	$text = preg_match("/^cde/u", "abcde", $matched); //マッチしません
</code>
<h3>$</h3>
対象文字列の終わりを意味します。<br />「m」モードだと各行の行末にマッチします。
<br /><br />
<code class="php">
	$text = preg_match("/bcd/u", "abcde", $matched); //$matchedには「bcd」が代入されます
	$text = preg_match("/bcd$/u", "abcde", $matched); //マッチしません
</code>
<h2>組み合わせ</h2>
正規表現パターンは「|(パイプ)」を用いて結合させることも可能です。
<h2>エスケープ</h2>
正規表現で「.(ドット)」を用いると任意の文字として扱われます。<br />同様に「\d」という文字列も任意の数字として扱われてしまいます。<br /><br />このように正規表現で特別な意味を持つ文字(特殊文字)を単なる文字として用いるためには特殊文字の前に「\(バックスラッシュ)」を付ける必要があります。
<p class="r">「\(バックスラッシュ)」は文字コードによっては円マークで表示されます。</p>
正規表現中でエスケープが必要な文字は以下の通りです。
<br /><br />
<ul>
	<li>「.」</li>
	<li>「\」</li>
	<li>「*」</li>
	<li>「+」</li>
	<li>「?」</li>
	<li>「()」</li>
	<li>「{}」</li>
	<li>「[]」</li>
	<li>「^」</li>
	<li>「$」</li>
	<li>「|」</li>
</ul>
