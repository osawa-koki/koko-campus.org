<h4>[^]</h4>
スクエアブラケットの否定バージョンです。<br />[^あいう]なら「あいう」以外の文字を意味します。
<br /><br />
<code class="php">
	preg_match("/[^めん]/u", "らーめん", $matched); //$matchedには「ら」「ー」が代入されます
</code>
