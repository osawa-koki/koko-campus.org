<h2>正規表現を用いる関数</h2>
正規表現とともに使用される関数には以下のものがあります。<br />PCRE関数とも呼ばれます。
<br />
<ul>
	<li>preg_match_all</li>
	<li>preg_match</li>
	<li>preg_replace</li>
	<li>preg_filter</li>
	<li>preg_grep</li>
	<li>preg_split</li>
</ul>
<p class="r">ちなみにこれらの関数の先頭にある「preg」は正規表現「regix」という意味です。</p>
<h3>preg_match_all</h3>
ある文字列から正規表現にマッチする全ての文字列を配列形式で取得します。<br />この関数では正規表現は「g」を書かなくても「g」モードで実行されます。<br /><br />「preg_match_all(正規表現, 対象文字列, マッチした配列を代入する変数)」と書きます。
<br /><br />
<code class="php">
	$str = "xxx123xxx456xxx789";
	preg_match_all("/\d+/", $str, $matched); //$matchedには「123」「456」「789」が代入されます
	echo $matched[0]; //「123」と表示される
	echo $matched[1]; //「456」と表示される
	echo $matched[2]; //「789」と表示される
</code>
<h3>preg_match</h3>
単にある文字列が対象の文字列に含まれるかどうかをチェックするためだけなら「preg_match()」関数を使用します。<br /><br />マッチしたは戻り値に「1」を返し、マッチしなかった場合は「0」を返すため、これで判定します。
<br /><br />
<code class="php">
	$str = "xxx123xxx";
	$exist = preg_match("/\d+/", $str); //$boolには「1」が代入される
	if ($exist === 1) {
		//マッチした際の処理
	} else {
		//マッチしなかった際の処理
	}
</code>
<h3>preg_replace</h3>
ある文字列の中の一定のパターンを別の文字列に置換します。<br /><br />「preg_replace(正規表現, 置換後文字列, 対象の文字列)」で書きます。
<br /><br />
<code class="php">
	$str = "xxx123xxx456xxx789";
	$replaced = preg_replace("/\d+/", "数字", $str); //$replacedには「xxx数字xxx数字xxx数字」が代入されます
</code>
<br />
また、置換後文字列に「""(空文字)」を指定することで対象のパターンを削除することも可能です。
<br /><br />
<code class="php">
	$str = "xxx123xxx456xxx789";
	$replaced = preg_replace("/\d+/", "", $str); //$replacedには「xxxxxxxxx」が代入されます
</code>
<br />
対象の文字列には配列を指定することも可能です。<br />この場合は戻り値も配列形式で返されます。
<br /><br />
<code class="php">
	$ary = array(
		"xxx123xxx", "xxx456xxx", "xxx789xxx"
	);
	$replaced = preg_replace("/\d+/u", "数字", $ary); //$replacedには「xxx数字xxx」「xxx数字xxx」「xxx数字xxx」が代入される
</code>
<h3>preg_filter</h3>
「preg_replace()」とほとんど同じです。<br /><br />戻り値がマッチした結果 (を変換したもの) のみを返すという点で異なりますが、通常の使用では気にする必要はありません。
<h3>preg_grep</h3>
ある配列内の要素に対して正規表現検索を行って、マッチする文字列だけを含む新たな配列を戻り値として返します。
<br /><br />
<code class="php">
	$ary = array(
		"abcde", "fg@ij", "k@mno", "pqr@@", "uvwxwz"
	);
	$matched = preg_grep("/[^@]/", "ary"); //$matchedには「abcde」「uvwxyz」が代入される
</code>
<h3>preg_split</h3>
ひとつ前の「配列の操作」の授業で文字列をある文字(区切り文字)で分割して配列を作成する「explode()」関数について学びましたね♪<br /><br />今回は区切り文字の部分に正規表現を使用します。<br /><br />「preg_split(正規表現, 対象の文字列)」で書きます。
<br /><br />
<code class="php">
	$str = "あぁ,疲れたなぁ、あと少し,がんばろう!";
	$ary = preg_split("/,|、/u", $str); //$aryには「あぁ」「疲れたなぁ」「あと少し」「がんばろう!」が格納されます
</code>
