<h2>正規表現とは</h2>
ある文字列から、一定のパターンの文字列を検索・置換するための技術です。<br /><br />例えば「101-8301」「1018301」は郵便番号として適切ですが、「koko-love」「1010-8301」「10-10-10-10」は郵便番号ではないですよね、、、<br /><br />こういった文字列のパターンを正規表現で定義します。<br /><br />正規表現について現在僕が勉強している本は「500」ページ以上あるんです、、、<br />とっても奥が深いです。<br /><br />ここでは簡単な、かつ良く用いる正規表現について学びましょう♪<br /><br />興味がある方は正規表現入門も学んでみて下さい。<br />とっても面白いですよ♪
<h2>書き方</h2>
phpでは正規表現は文字列として書き出します。<br />具体的には「"正規表現"」としますが、この文字列をデリミタ(区切り文字)で囲む必要があります。<br /><br />デリミタとして使用可能な文字はいくつかありますが、ここではもっとも一般的な「//(スラッシュ)」を用います。<br /><br />「"/正規表現/"」こんな感じですね♪<br /><br />また正規表現を用いた関数については後ほど学習しますが、正規表現の説明に用いるため「preg_match()」関数だけ簡単にここで説明します。
<h3>preg_match()</h3>
ある文字列に含まれる、正規表現で示されたパターンにマッチする部分を取得します。
<br /><br />
preg_match("/正規表現/", "対象文字列", マッチした文字列を格納する変数);
<h2>モード</h2>
正規表現にはモードという概念が存在して、正規表現を判定するアルゴリズムそのものの振る舞いを指定します。<br />具体的には以下の4つがあります。
<br />
<ul>
	<li>u</li>
	<li>i</li>
	<li>m</li>
	<li>s</li>
</ul>
<h3>u</h3>
マルチバイトモードで、原則としてこれを用いることをオススメします。<br />使用しなくても良い場面はチェック対象が半角英数字だけと限定されている場合です。
<h3>i</h3>
大文字と小文字を区別しない設定です。<br />このモードに設定すると「APPLE」「Apple」「apple」も同じ文字列とします。
<h3>m</h3>
マルチラインモードと呼ばれ、後ほど説明する「^」と「$」の動作を変更します。
<h3>s</h3>
後ほど説明しますが、「.」の動作を変更します。
<h2>文字の指定</h2>
ここでは、以下の2つの記法を学びます。
<br />
<ul>
	<li>「.(ドット)」</li>
	<li>「[](スクエアブラケット)」</li>
</ul>
<h3>「.(ドット)」</h3>
任意の一文字(改行文字を除く)を表します。<br />正規表現を「s」モードに設定すると改行文字にもマッチします。
<br /><br />
<code class="php">
	preg_match("/./", "らーめん", $matched); //$matchedには「ら」が代入されます。
	preg_match("/./u", "らーめん", $matched); //$matchedには「ら」「ー」「め」「ん」が代入されます
</code>
<h3>「[](スクエアブラケット)」</h3>
「[]」内の文字のうちのいずれかを意味します。
<br /><br />
<code class="php">
	preg_match("/[かた]/u", "らーめん", $matched); //マッチしません
	preg_match("/[かため]/u", "らーめん", $matched); //$matchedには「め」が代入されます
</code>
<h4>[-]</h4>
「[](スクエアブラケット)」の中に「-(ハイフン)」を入れることで範囲を指定できます。<br />例えば「[a-z]」で「abcdefg...xyz」を意味し、「[0-9]」で「0123456789」を意味します。<br /><br />範囲といっても文字コードによるので全てを理解することは不可能ですので、こんなものがあるんだな程度でok!です。<br /><br />良く用いる範囲指定を紹介しますね♪
