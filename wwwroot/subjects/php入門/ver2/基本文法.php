<h3>switch文</h3>
ポケモンソードシールドのカセットとswitchを持って家電量販店に行くと色違いのザシアンとザマゼンタが貰えるイベントがあったんですけど、22才の僕を10歳くらいの子たちが囲むように並んできてすごく恥ずかしかったです。<br /><br />どうでもいいですね、、、<br />switch文ではif文をより簡単に記述することができます。<br />特にif文が何個も続く際に用いられます。
<code class="php">
	switch ($i) {
		case 1:
			echo "1です。";
			break;
		case 2:
			echo "2です。";
			break;
		case 3:
			echo "3です。";
			break;
		default:
			echo "1,2,3のいずれでもない。"
	}
</code>
「switch」の後の条件式($i)がcaseの後のデータと等しければそれ以降の処理が実行されます。<br /><br />phpのswitch文は一度条件に合致するとそのあとの全てのcaseを実行してしまうため、最後に「break;」を付けて処理を中断させて下さい。<br /><br />いずれにも一致しなかった場合は「default」の後の文が実行されます。<br /><br />またswitchでの式の比較は「===」ではなく「==」であり、型が異なっていても値が等しければ「true」となることに注意してください。
<h3>反復処理</h3>
条件を満たす限りある処理をし続けます。
<img src="../pics/cmd-for.gif" alt="for" />
こんな感じです。<br />反復処理には主に以下の2つの方法があります。
<ul>
	<li>for文</li>
	<li>while文</li>
</ul>
<h4>for文</h4>
通常は回数を指定して処理を実行する際に用いられます。<br />例えば100回挑戦(challenge)する際には以下のようなコードを書きます。
<code class="php">
	for ($i = 0; $i &lt; 100; $i++) {
		challenge();
	}
</code>
for文に続く括弧の中は以下の構成になっています。
<div class="scroll-600w">
	<table border="1">
		<tbody>
			<tr>
				<th>$i = 0;</th>
				<th>$i < 100;</th>
				<th>$i++</th>
			</tr>
			<tr>
				<td>初期値として$iに0を設定します。</td>
				<td>「{}」内の処理を$iが100未満の限り実行し続けます。</td>
				<td>「{}」内の処理が完了する度、$iの値を「1」増加させます。<br />「$i = $i + 1」の短縮系です。</td>
			</tr>
		</tbody>
	</table>
</div>
<h4>while文</h4>
処理をひたすらし続けて、ある条件が発生したらループから抜け出させる(break)際に用いられます。
<code class="php">
	while (true) {
		$rec = $stmt->fetch(PDO::FETCH_ASSOC);
		if ($rec === false) {
			break;
		}
		$name_list[] = $rec["name"];
	}
</code>
whileの後の条件式が「true」であれば「{}」内の処理をするんですけど、一般的な用い方は条件式に「true」を入れて無限ループさせて、ある条件を満たしたら強制的に「break;」を用いてループ外へ脱出させます。<br /><br />わかりにくいですよね、、、<br /><br />最初のうちはあまり使わないので理解できなくてok!です。<br />後ほどデータベースからデータを取得する際に使用します。
