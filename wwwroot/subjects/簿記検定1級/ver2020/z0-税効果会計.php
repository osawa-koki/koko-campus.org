					<li>減価償却費</li>
					<li>棚卸資産の評価額</li>
					<li>その他有価証券の評価差額</li>
				</ul>
			</td>
		</tr>
		<tr>
			<th>永久差異</th>
			<td>
				永久に解消されない差異を言います。
				<ul class="no">
					<li>寄付金の損金不算入</li>
					<li>交際費の損金不算入</li>
				</ul>
			</td>
		</tr>
	</tbody>
</table><br>
税効果会計の適用対象となるのは<strong>一時差異</strong>です。
