<h2>法人税等の調整</h2>
税効果会計によって法人税等を算出するために用いられる課税所得を計算したら、実際に支払う法人税等の額を調整します。<br><br>例えば当期純利益が300円、課税所得が400円で、法人税率が40%とします。<br>法人税等の額を計算すると会計上では120(300 &times; 0.4)円、税法上では160(400 &times; 0.4)円で、40円分少なく算出されていることが分かります。<br><br>このため、40円分の未払いの法人税等を計上する仕訳をします。
