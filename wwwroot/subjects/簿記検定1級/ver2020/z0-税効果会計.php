<h2>税効果会計</h2>
<p>会計上の利益を税法上の利益(課税所得)に調整することを言います。</p>
<div class="f">
	!税効果会計!
	税引前当期純利益(会計上の利益)=>課税所得(税法上の利益)
</div>
会計上では「収益 - 費用」で利益を計算しますが、税法上では「益金 - 損金」で利益を計算します。<br><br>税効果会計では、会計上の利益を税法上の利益に合わせる調整をします。
<h2>一時差異と永久差異</h2>
会計上の利益と税法上の差異は「<strong>一時差異</strong>」と「<strong>永久差異</strong>」に分類されます。<br>
