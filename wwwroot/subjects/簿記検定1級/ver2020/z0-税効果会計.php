	法人税等調整額40,繰延税金負債40
</div>
<h2>基本原理</h2>
税効果会計の仕訳は以下の3つの手順で考えます。<br>
<ol>
	<li>会計上の利益と税法上の利益のどちらが大きくなるか判断</li>
	<li>法人税率をかけて支払う法人税等がいくら多く(少なく)なるか計算</li>
	<li>法人税等を加減算処理</li>
</ol><br>
<h2>税効果会計特例</h2>
以下の2つの処理に関しては、会計上の処理と税法上の処理が大きく異なるため特殊な税効果会計が求められます。<br>
<ul>
	<li>その他有価証券の評価差額</li>
	<li>圧縮記帳(積立金方式)</li>
</ul>
<h3>その他有価証券の評価差額</h3>
その他有価証券に関しては決算時に評価替えをしますが、<span class="u">税法上はその他有価証券の評価替えを認めていません。</span><br><br>そのため、評価差額に法人税率をかけた額を<strong>繰延税金資産(繰延税金負債)</strong>として計上します。<br><br>繰延税金資産(繰延税金負債)の相手科目は、部分原価直入法を採用していてかつ評価差損が発生した場合は<strong>法人税等調整額</strong>、それ以外では<strong>その他有価証券評価差額金</strong>で処理します。<br><br>翌期首においては、相手科目をその他有価証券評価差額金で処理している場合のみ振り戻す処理をします。<br>(その他有価証券は洗替法を採用しているため)
<h3>圧縮記帳(積立金方式)</h3>
圧縮記帳で積立金方式を採用した場合、会計上は圧縮損を計上しませんが税法上は圧縮損を計上することができます。<br><br>圧縮損を計上した際は決算時において税効果を控除すると同時に、繰越利益剰余金を圧縮積立金に振り替える額を税効果相当を控除した額とします。


