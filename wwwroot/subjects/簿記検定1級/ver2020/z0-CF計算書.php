<h4>間接法</h4>
<p>損益計算書の税引前当期純利益を基に、それを必要な項目を加減して調整することで営業CFを算出します。</p>
以下の5つの手順を踏みます。<br>
<ol>
	<li>非資金損益項目の加減</li>
	<li>営業外損益・特別損益の加減</li>
	<li>売上債権・仕入債務・棚卸資産の増減額の加減</li>
	<li>利息・配当金の処理</li>
	<li>投資活動・財務活動にも属さない項目の処理</li>
</ol>
<h5>非資金損益項目の加減</h5>
貸倒引当金繰入や減価償却費などの現金の支出を伴わない項目(非現金支出項目)を加減します。
<h5>営業外損益・特別損益の加減</h5>
営業外損益・特別損益は営業CFには該当しないため、これを加減します。
