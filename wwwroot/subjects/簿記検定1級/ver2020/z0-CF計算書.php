<h2>CF計算書の意義</h2>
損益計算書項目(収益・費用)の認識基準はそれぞれ実現主義・発生主義でした。<br><br>しかしながら、これでは実際の現金の収入・支出を把握できないため黒字倒産の危険性が高まります。<br><br>そのような問題を克服するための財務諸表がキャッシュフロー計算書(CF計算書)です。<br>
<span class="r">
	!黒字倒産!
	?損益計算書上では利益を挙げているにもかかわらず、資金(現金)が不足しているため支払ができずに倒産することを言います。<br>倒産企業の半数近くが黒字倒産によるものと報告されています。(2021年10月30日時点)<br>※<a href="https://www.tsr-net.co.jp/news/analysis/20190215_02.html">東京商工リサーチ</a>より?
</span>
<h2>CF計算書項目の定義</h2>
キャッシュフロー計算書におけるキャッシュは以下の2つを意味します。<br>
<ul>
	<li>現金</li>
	<li>現金同等物</li>
</ul>
<h3>現金</h3>
「<strong>手許現金 + 要求払預金(普通預金 + 当座預金 + 通知預金)</strong>」を指します。<br>
<h3>現金同等物</h3>
<span class="u">容易に換金可能かつ価格変動リスクが僅少な短期(3カ月いない)投資</span>を言います。<br><br>具体的には「<strong>定期預金 + 譲渡性預金 + コマーシャルペーパー + 公社債投信</strong>」が該当します。<br>
<span class="r">
	!譲渡性預金!
	?銀行が発行する無記名の預金証書を言います。?
</span><br>
<span class="r">
	!コマーシャルペーパー!
	?企業が発行する短期の約束手形を言います。?
</span><br>
<h2>CF計算書の分類</h2>
CF計算書では会社の活動を以下の3つに分類します。<br>
<ul>
	<li>営業活動</li>
	<li>投資活動</li>
	<li>財務活動</li>
</ul><br>
<h3>営業CF</h3>
商品の仕入・販売等の営業活動から生じるCFのほか、投資活動・財務活動に分類されない保険金・法人税等のCFを記載します。
<h3>投資CF</h3>
有価証券・固定資産の購入・売却や貸付金などから生じるCFを記載します。
<h3>財務CF</h3>
社債の発行・返済や株式の発行・配当金、借入金に関するCFを記載します。
<h2>利息・配当金の表示区分</h2>
利息と配当金の表示区分に関しては以下の2つの区分方法が適用可能です。<br>
<ul>
	<li>P/L項目かで区分</li>
	<li>活動で区分</li>
</ul>
<h3>P/L項目かで区分</h3>
<span class="u">「受取利息・受取配当金・支払利息」は営業CF</span>、<span class="u">「支払配当金」は財務CF</span>に表示します。
<h3>活動で区分</h3>
<span class="u">「受取利息・受取配当金」は投資CF</span>、<span class="u">「支払利息・支払配当金」は財務CF</span>に表示します。
<h2>CF計算書の作成</h2>
<h3>営業CF</h3>
営業CFの計算方法には以下の2つがあります。<br>
<ul>
	<li>直接法</li>
	<li>間接法</li>
</ul><br>
