決算時において前払費用・前受収益のうち、当期分を為替差損益に振り替えます。<br><br>また決算日の翌日から1年を超えて決済日が到来する場合には、前払費用・前受収益を<strong>長期前払費用・長期前受収益</strong>に振り替えます。

