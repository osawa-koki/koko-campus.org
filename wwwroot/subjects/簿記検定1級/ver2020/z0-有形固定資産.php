<h2>資産除去債務</h2>
有形固定資産の使用後の除去に関して発生する費用の処理方法についてです。<br><br>簡単に言えば「<strong>負の残存価額</strong>」ですが、残存価額との大きな違いは<span class="u">資産除去債務は時間価値を考慮して会計処理をする</span>ことです。<br>
<span class="r">
	!時間価値!
	?「現在の価値」 &times; abc => 「将来の価値」の「abc」の部分のことを言います。<br>一般的に利子(利息)が該当します。<br><br>ここでは逆から捉えて、<span class="u">「将来の価値」 &divide; 「abc」 => 「現在の価値」</span>を使用します。?
</span><br>
資産除去債務は<strong>負債</strong>として処理されますが、資産除去債務は将来の価値を表す価額であるため現在の価値に割り引いて計上します。
