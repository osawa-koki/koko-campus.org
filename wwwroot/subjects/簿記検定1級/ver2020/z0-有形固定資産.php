<h2>減損会計</h2>
資産に関しては原則として取得原価モデルを採用するため時価評価はしませんが、資産の収益性の低価が明らかな場合には帳簿価額を引き下げる処理をします。<br><br>これを減損会計と言い、以下の4つの手順を踏みます。<br>
<ol>
	<li>対象資産のグルーピング</li>
	<li>減損兆候の把握</li>
	<li>減損損失の認識</li>
	<li>減損損失の測定</li>
</ol>
<h3>対象資産のグルーピング</h3>
資産をキャッシュフローを生み出す最小単位でグルーピングします。
<h3>減損兆候の把握</h3>
あるグループが減損を生じさせる状況にあるかどうかを定性的に判断します。
<h3>減損損失の認識</h3>
将来キャッシュフローの合計が帳簿価額よりも低ければ減損損失を認識します。<br>ここでの将来キャッシュフローの値は<span class="u">割引前</span>のものを用います。<br><br>
<div class="column">
	将来キャッシュフローの見積期間が20年を超える場合、20年を超える部分に関しては「<strong>20年経過時点の正味売却価額</strong>」か「<strong>使用価値</strong>」のいずれか高い額で計算します。
</div>
<h3>減損損失の測定</h3>
減損損失対象の資産に関して、帳簿価額を<strong>回収可能価額</strong>まで切り下げます。<br>
<span class="r">
	!回収可能価額!
	?「<strong>正味売却可能価額(時価 - 処分費用)</strong>」か「<strong>使用価値(将来キャッシュフローの割引現在価値合計)</strong>」のいずれか高い方を言います。?
</span>
帳簿価額と回収可能価額との差額は<strong>減損損失</strong>で処理します。
<div class="column">
	資産グループで減損損失が認識された場合には、資産グループで減損損失を認識した後に<span class="u">各資産の帳簿価額に基づいて</span>構成する資産に配分します。
</div>
