<h2>圧縮記帳</h2>
政府から補助金(国庫補助金・工事負担金)を受け取った際には、<strong>国庫補助金収入・工事負担金収入</strong>で処理します。<br><br>これらは収益として記録されますがこの収益に対して税金が課されると補助金の意義を否定することになるため、固定補助金の額だけ固定資産を圧縮させることで一時的に租税を回避します。<br><br>圧縮記帳の方法には以下の2つがあります。<br>
<ul>
	<li>直接減額法</li>
	<li>積立金方式</li>
</ul>
<h3>直接減額法</h3>
<p>取得した固定資産に関して、補助金の額だけ直接減額する方法です。</p>
