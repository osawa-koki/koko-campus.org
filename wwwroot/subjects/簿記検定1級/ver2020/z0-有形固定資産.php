<h2>取得原価の決定</h2>
棚卸資産や有価証券と同様に、有形固定資産の取得原価は<strong>付随費用</strong>を含んだ金額で計上します。<br>有形固定資産の取得原価の決定には以下の注意点があります。<br>
<ul>
	<li>一括購入</li>
	<li>自家建設</li>
	<li>現物出資</li>
	<li>交換取引</li>
	<li>廉価取得・無償取得</li>
</ul><br>
<span class="r">
	!廉価取得!
	?通常の価格よりも安く購入することを言います。?
</span>
<h3>一括購入</h3>
複数の資産(建物と土地など)を取得した場合は、当該有形固定資産の<strong>時価の比率</strong>によって両資産に按分します。
<h3>自家建設</h3>
適切な原価計算基準に基づいて算出された製造原価をもって取得原価とします。<br><br>また自家建設にかかる借入金の利息(借入資本利子)のうち、有形固定資産の稼働前の期間に属するものは取得原価に含めることができます。
<h3>現物出資</h3>
現物出資によって有形固定資産を取得した場合には、当該有形固定資産の時価をもって取得原価とします。
<h3>交換取引</h3>
信頼性のある支出価格をもって取得原価とします。
<h4>有価証券との交換</h4>
有価証券の時価は一般に高い信頼性があると推定されるため、交換時の有価証券の時価をもって取得原価とします。
<h4>有形固定資産同士の交換</h4>
相手に渡した資産の帳簿価額をもって取得原価とします。
<h4>廉価取得・無償取得</h4>
完全に取得原価モデルを採用すると無償取得ないしは廉価取得した資産の価格を適切に財務諸表に反映できないという問題点があります。<br><br>そこで、廉価取得・無償取得した資産は<strong>贈与時の時価</strong>をもって取得原価とします。
<h2>有形固定資産の割賦購入</h2>
