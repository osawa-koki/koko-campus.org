<h2>有形固定資産の売却・除却</h2>
<h3>売却</h3>
有形固定資産の帳簿価額と売却価額との差額を<strong>固定資産売却損益</strong>として処理します。
<h3>除却</h3>
有形固定資産の処分価値を<strong>貯蔵品</strong>として記録して、処分価値と帳簿価額との差額を<strong>固定資産除却損益</strong>として処理します。
<div class="t">
	!!
	減価償却累計額500,備品700
	貯蔵品150,
	固定資産除却損50,
</div>
貯蔵品を売却した場合は差額を<strong>貯蔵品売却損益</strong>、廃棄した場合は差額を<strong>固定資産廃棄損</strong>として処理します。
