		</tr>
		<tr>
			<th>製造間接費</th>
			<td>使用先である製品が不明である原価を言います。</td>
		</tr>
	</tbody>
</table>
<h4>操業度との関連</h4>
<table class="exp">
	<tbody>
		<tr>
			<th>変動費</th>
			<td>操業度の比例して発生している原価を言います。</td>
		</tr>
		<tr>
			<th>固定費</th>
