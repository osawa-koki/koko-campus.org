<h2>原価概念</h2>
<h3>原価要件</h3>
原価とは、「<span class="u">経営給付に直接関連付けられる支出</span>」と定義されますが、具体的には以下の4つの原価要件を満たす必要があります。<br />
<ul>
	<li>経営価値消費性</li>
	<li>給付関連性</li>
	<li>経営目的関連性</li>
	<li>正常性</li>
</ul>
<br />
したがって、これらの要件を満たさない「営業外費用・特別損失・税法上でのみ認められている項目・その他利益剰余金」は原価に該当しません。
<h3>原価と費用</h3>
「原価」と似た用語として「費用」がありますが、会計の世界においては両者は明確に区別されます。
