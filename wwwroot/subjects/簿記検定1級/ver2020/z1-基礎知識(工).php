<h3>原価の構成</h3>
上記の原価を要件を満たす項目は大きく以下の3つから構成されます。<br />
<ul>
	<li>製造原価<br />(「工場」で発生)</li>
	<li>販売費<br />(「営業所」で発生)</li>
	<li>一般管理費<br />(「本社」で発生)</li>
</ul>
<br />
また、この3つを総称して<strong>総原価</strong>と言います。
<h3>原価の分類</h3>
原価は以下の4つの観点から分類することができます。<br />
<ul>
	<li>形態別</li>
	<li>製品との関連</li>
	<li>操業度との関連</li>
	<li>管理可能性</li>
</ul>
<br />
<h4>形態別</h4>
