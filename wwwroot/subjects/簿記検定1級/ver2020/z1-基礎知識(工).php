		</tr>
	</tbody>
</table>
<h4>管理可能性</h4>
<table class="exp">
	<tbody>
		<tr>
			<th>管理可能費</th>
			<td>ある一定の階層の管理者によって管理ができる原価を言います。</td>
		</tr>
		<tr>
