<h2>無形固定資産の償却</h2>
無形固定資産の減価償却は<span class="u">残存価格は「0」</span>、<span class="u">定額法で</span>、<span class="u">直接法で</span>行います。
<h2>ソフトウェアの分類</h2>
ソフトウェアは目的によって以下の4つに分類されます。<br>
<ul>
	<li>研究開発目的</li>
	<li>市場販売目的</li>
	<li>受注制作</li>
	<li>自社利用</li>
</ul><br>
ソフトウェアの制作にかかる費用は目的によって以下のように処理します。
