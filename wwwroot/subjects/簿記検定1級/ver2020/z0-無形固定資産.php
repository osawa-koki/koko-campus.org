ここでは、無形固定資産として処理する「市場販売目的ソフトウェア」と「自社利用目的ソフトウェア」について扱います。
<h3>市場販売目的ソフトウェアの償却</h3>
原則として「<strong>見込販売数量・見込販売収益</strong>(生産高比例法と同様の処理)」に基づいて算出された減価償却費を用いますが、この方法によって算出された減価償却費が「<strong>未償却残高 &divide; 残存有効期間</strong>」を下回った場合はこの「未償却残高 &divide; 残存有効期間」を減価償却費とします。<br><br>また次期以降の見込販売収益がソフトウェアの期末未償却残高を下回っていた場合は減損会計を適用してその超過分を当期の費用として処理します。
<h3>自社利用のソフトウェアの償却</h3>
利用可能期間(原則5年)にわたって定額法で償却します。
