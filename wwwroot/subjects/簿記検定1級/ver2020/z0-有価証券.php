<h2>有価証券の分類</h2>
<p>有価証券はその保有目的によって以下の4つに分類されます。</p>
<ul>
	<li>売買目的有価証券</li>
	<li>満期保有目的債権</li>
	<li>子会社株式・関連会社株式</li>
	<li>その他有価証券</li>
</ul>
<h3>売買目的有価証券</h3>
短期的に保有することが目的であるため、<strong>流動資産</strong>勘定に<strong>有価証券</strong>として表示します。
<h3>満期保有目的債権</h3>
長期的に保有することが目的であるため、<strong>固定資産(投資その他の資産)</strong>勘定に<strong>投資有価証券</strong>として表示します。<br><br>但し、満期日が一年以内の場合は<strong>流動資産</strong>勘定に<strong>有価証券</strong>として表示します。
<h3>子会社株式・関係会社株式</h3>
長期的に保有することが目的であるため、<strong>固定資産(投資その他の資産)</strong>勘定に<strong>関係会社株式</strong>として表示します。
<h3>その他有価証券</h3>
長期的に保有することが目的であるため、<strong>固定資産(投資その他の資産)</strong>勘定に<strong>投資有価証券</strong>として表示します。
