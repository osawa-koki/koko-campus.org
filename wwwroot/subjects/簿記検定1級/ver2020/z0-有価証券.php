<h2>有価証券の評価</h2>
<h3>売買目的有価証券</h3>
売買目的有価証券は決算日において評価替えをします。<br>評価差額は「<strong>有価証券評価損益</strong>」として処理します。<br><br>評価差額は翌期首においては「<strong>切放法</strong>」または「<strong>洗替法</strong>」で処理します。<br>
<span class="r">
	!切放法!
	?評価損益を翌期首において振り戻さない方法です。<br><br>根拠としては<strong>時価評価モデル</strong>が採用されます。?
</span><br>
<span class="r">
	!洗替法!
	?評価損益を翌期首において振り戻す方法です。<br><br>根拠としては<strong>取得原価モデル</strong>が採用されます。?
</span><br>
<h3>満期保有目的債権</h3>
満期保有目的債権は決算日において原則として評価替えをしません。<br><br>但し、額面価額と取得原価の差額が金利調整差額と認められる場合には<strong>償却原価法</strong>によって差額を各期に取得原価に加減する処理をします。<br>
<span class="r">
	!償却原価法!
	?額面価額と取得原価の差額を金利分の先払いとして推定する処理方法を言います。<br><br>具体的な会計手法として<strong>利息法</strong>(原則)と<strong>定額法</strong>(例外)があります。?
</span>
