<h4>定額法</h4>
金利調整差額を償却期間にわたって一定の額を償却される方法です。
<h3>子会社・関連会社株式</h3>
子会社・関連会社株式は決算時において評価替えをしません。
<h3>その他有価証券</h3>
その他有価証券はその性質上、売買目的有価証券と似ているため決算時において時価で評価替えをします。<br><br>しかし、<span class="u">売買目的有価証券と異なりすぐに売却することを目的としていないため評価差額は原則としてP/Lに計上しません。</span><br><br>その他有価証券の評価差額は「<strong>全部純資産直入法</strong>」または「<strong>部分純資産直入法</strong>」によって処理します。<br><br>また、翌期首において評価差額を振り替える<strong>洗替法</strong>を採用します。
<h4>全部純資産直入法</h4>
その他有価証券の評価差額(評価益・評価損の両方)を<strong>その他有価証券評価差額</strong>(純資産)として計上する方法です。
<h4>部分純資産直入法</h4>
その他有価証券の評価差額のうち、<span class="u">評価益はその他有価証券評価差額として純資産</span>の部に、<span class="u">評価損は投資有価証券評価損として費用</span>の部に計上する方法です。
<h3>減損処理</h3>
売買目的有価証券以外の有価証券は原則として取得原価をもって貸借対照表価額としますが、時価が著しく下落した際には評価替え(減損処理)が強制されます。<br><br>減損処理の方法には以下の2通りあります。<br>
<ul>
	<li>強制評価減</li>
	<li>実価法</li>
</ul><br>
<h4>強制評価減</h4>
「<strong>時価が著しく下落し、かつ、回復の見込みが見込み認められる場合を除いて</strong>」、有価証券を時価で評価する。
<h4>実価法</h4>
時価の測定が困難な有価証券に関して、当該株式発行会社の財政状態が著しく悪化した場合に、帳簿価額を実質価値まで低下させます。<br><br>実質価値は「<strong>BPS &times; 所有株式数</strong>」で計算します。<br>
<span class="r">
	!BPS!
	?「Bookvalue Per Share」の略で「一株当たり純資産」と訳されます。<br><br>「純資産(資産 - 負債) &divide; 発行済株式数」で算出します。?
</span>
<h2>保有目的の変更</h2>
変更時の振替価格は変更前の保有目的によって判断します。<br>(変更時に一度その有価証券を売却して、再度保有目的を替えて取得したものと仮定)<br><br>時価評価する売買目的有価証券とその他有価証券の評価差額はそれぞれ、有価証券評価損益と投資有価証券評価損益で処理します。
