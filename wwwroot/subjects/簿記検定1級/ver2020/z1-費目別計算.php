<h3>支払計算</h3>
「基本賃金 + 加給金 + 諸手当」から預り金を控除した額を支給します。
<div class="t">
	!!
	賃金300,現金250
	,預り金50
</div>
<span class="r">
	!加給金!
	?基本賃金以外に支払われる、作業に直接関係のある手当を言います。<br /><br />作業に直接関係のない手当は諸手当として処理されます。?
</span>
<h3>消費計算</h3>
労務費の消費計算では、給与計算期間と原価計算期間のズレ(支払期間と消費期間のズレ)を解消するため以下の4つの処理をします。<br />
<ol>
	<li>前月未払賃金の再振替仕訳</li>
	<li>当月支給分の処理</li>
	<li>当月未払賃金の振替</li>
	<li>当月消費分の処理</li>
</ol>
<h4>実際消費賃金での計算(直接工)</h4>
「<strong>実際消費賃率 &times; 実際作業時間</strong>」から直接工の実際消費賃金を計算します。<br /><br />実際消費賃率は「<strong>直接工の消費賃金 &divide; 就業時間</strong>」で算出します。
<h4>予定消費賃金での計算(直接工)</h4>
「<strong>予定消費賃率 &times; 実際作業時間</strong>」から直接工の予定消費賃金を計算します。<br /><br />予定消費賃率は「<strong>直接工の年間予定賃金・手当総額 &divide; 年間予定就業時間</strong>」で算出します。
<div class="column">
	月末には「予定消費賃金 - 実際消費賃金」を<strong>賃率差異</strong>として計上します。
</div>
<h4>消費賃金の計算(間接工)</h4>
間接工の消費賃金は<strong>要支払額</strong>とします。<br /><br />その他の事務員などへの給料も同様です。
<h4>定時間外作業手当の処理</h4>
定時労働時間を超えて労働した場合の労務費のうち、基本賃金部分は通常の基本賃金として処理して、残業代部分は定時間外作業手当として認識して間接労務費として製造間接費に計上します。
<h2>経費会計</h2>
特定の製品に使用されたことが明確な原価(外注加工賃・特許料使用料・試作費・etc...)を直接経費として、それ以外は間接経費とします
<h3>外注加工賃の処理</h3>
外注加工賃は以下の3つの処理方法が認められます。<br />
<ul>
	<li>無償支給 &times; 直接経費処理</li>
	<li>無償支給 &times; 部品原価処理</li>
	<li>有償支給 &times; 部品原価処理</li>
</ul>
<br />
加工品を直ちに現場に引き渡す場合には直接経費処理をして、加工品を一旦部品とする場合には部品原価処理をします。
<h4>無償支給 &times; 直接経費処理</h4>
ここでは外注加工賃を材料のように扱う処理をします。<br /><br />具体的には以下の3つの仕訳を行います。<br />
<ol>
	<li>消費計算(通常材料分)</li>
	<li>支払計算(外注加工分)</li>
	<li>消費計算(外注加工分)</li>
</ol>
<br />
<div class="t">
	!消費計算(通常材料分)!
	仕掛品300,材料300
</div>
<div class="t">
	!支払計算(外注加工分)!
	外注加工賃100,現金100
</div>
<div class="t">
	!消費計算(外注加工分)!
	仕掛品100,外注加工賃100
</div>
<h4>無償支給 &times; 部品原価処理</h4>
部品勘定という仕掛品に似た勘定をもうひとつ設けてこれを経由させる方法です。<br /><br />具体的には以下の4つの処理をします。<br />
<ol>
	<li>支払計算(外注加工分)</li>
	<li>消費計算(通常材料分)</li>
	<li>消費計算(外注加工分)</li>
	<li>部品の振替</li>
</ol>
<br />
<div class="t">
	!支払計算(外注加工分)!
	外注加工賃100,現金100
</div>
<div class="t">
	!消費計算(通常材料分)!
	部品300,材料300
</div>
<div class="t">
	!消費計算(外注加工分)!
	部品100,外注加工賃100
</div>
<div class="t">
	!部品の振替!
	#加工品を部品の倉庫から取り出した際の処理#
	仕掛品400,部品400
</div>
<h4>有償支給 &times; 部品原価処理</h4>
<ol>
	<li>材料の販売</li>
	<li>加工品の受入</li>
	<li>売却購入差額の修正</li>
	<li>交付材料差額の控除</li>
</ol>
<br />
<div class="t">
