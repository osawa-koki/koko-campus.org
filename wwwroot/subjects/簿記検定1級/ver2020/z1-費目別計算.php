<h3>支払計算</h3>
<h4>実際価格での購入</h4>
材料を購入した際には、購入代価に引取費用と材料副費を加算して購入原価とします。<br /><br />材料副費が2種類以上の材料にかかっている場合には、何らかの基準を基にそれぞれの材料費に配賦します。<br /><br /><small>(材料副費を購入原価に含めない場合もあります)</small>
<div class="t">
	!!
	材料500,現金400
	,材料副費100
</div>
<div class="column">
	材料副費に関しては予定配賦額を用いることも可能です。<br /><br />「<strong>予定配賦額 = 予定配賦率 &times; 実際配賦基準数値</strong>」で算出します。<br /><br />予定配賦率は「1年間の材料配賦予想額 &divide; 1年間の予定配賦基準数値」で計算します。<月末には「予定配賦額 - 実際発生額」を材料副費配賦差異に振り替えます。
</div>
<h4>予定価格での購入</h4>
材料の購入原価は原則として実際の購入原価で計算しますが、予定価格での計算も容認されています。<br /><br />この予定価格での購入原価は「<strong>予定価格 &times; <span class="u">実際</span>数量</strong>」で算出します。<br /><br />また、実際の購入原価との差額は「<strong>材料受入価格差異((予定価格 - 実際価格) &times; 実際購入数量)</strong>」で把握されます。
<h3>消費計算</h3>
<h4>実際価格での消費</h4>
材料費の計算は、購入・消費の記録をしているかどうかによって方法が異なります。<br /><br />記録している場合は「<strong>消費単価 &times; 消費数量</strong>」を材料消費額とし、記録してない場合は購入原価をそのまま材料消費額とします。<br /><br />記録している場合の消費数量の算定には、継続記録法・棚卸計算法のいずれかを用います。<br />記録している場合の消費単価の算定には、先入先出法・総平均法のいずれかを用います。
<h4>予定価格での消費</h4>
材料消費計算において材料予定消費額(予定消費単価 &times; 実際消費数量)を用いることもできます。
<div>
