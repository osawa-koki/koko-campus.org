<h2>費目別計算の概要</h2>
費目別計算は大きく以下の2つからなります。<br />
<ol>
	<li>支払計算</li>
	<li>消費計算</li>
</ol>
<br />
支払計算では「材料費・労務費・経費」が発生した際に各勘定の借方に記帳して、消費計算では実際にそれらの原価を消費した際に「仕掛品・製造間接費」に振り替えます。
<div class="t">
	!支払計算!
	材料300,現金300
</div>
<div class="t">
	!消費計算!
	仕掛品200,材料300
	製造間接費100,
</div>
以下では、「材料費・労務費・経費」それぞれについて詳細に説明します。
<h2>材料費会計</h2>
材料費は製品との関連と形態別により、以下のように分類できます。
