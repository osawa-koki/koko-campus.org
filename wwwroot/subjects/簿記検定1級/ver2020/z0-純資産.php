	?「のれん &times; 1/2 + 繰延資産」<br><br>で算出します。?
</span>
<h2>新株予約権</h2>
<p>一定の期間に予め定められた価格で株式を購入する権利を言います。</p>
新株予約権をインセンティブとして自社の従業員に発行するストックオプションとして用いられることが多いです。
<h3>新株予約権の発行時の処理</h3>
新株予約権の払込金額をもって新株予約権に計上します。
<div class="t">
	!新株予約権の発行時!
	現金200,新株予約権200
</div>
<h3>新株予約権の行使時の処理</h3>
新株予約権を行使した際には、「新株を発行」するか「自己株式を移転」するかによって処理が変わります。
<div class="t">
	!新株予約権の行使(新株の発行))!
	@新株を発行した場合は、新株予約権を消滅させると同時に払込金額を合わせて、資本金に振り替えます。<br>ここでも、２分の１まで資本金とせずに資本準備金とすることが認められています。@
	新株予約権200,資本金500
	現金800,資本準備金500
</div>
<div class="t">
	!新株予約権の行使(自己株式の移転))!
	@自己株式を移転する場合には、自己株式の帳簿価額を減額すると同時に払込金額合計との差額を<strong>その他資本剰余金</strong>として処理します。@
	新株予約権200,自己株式900
	現金800,その他資本剰余金100
</div>
<h3>新株予約権の失効時の処理</h3>
新株予約権の権利行使がなされないまま権利行使可能期間が終了した場合には、新株予約権の帳簿価額を<strong>新株予約権戻入益(特別損失)</strong>に振り替えます。
<div class="t">
	!新株予約権の失効!
	新株予約権200,新株予約権戻入益200
</div>
