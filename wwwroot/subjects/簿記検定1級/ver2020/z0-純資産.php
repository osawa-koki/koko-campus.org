<h2>新株予約権付社債</h2>
新株予約権が付いた社債を言います。<br><br>新株予約権付社債は以下の2つに分類されます。<br>
<ul>
	<li>転換社債型新株予約権付社債(CB)</li>
	<li>転換社債型新株予約権付社債以外の新株予約権付社債(ワラント債)</li>
</ul>
<h3>CB</h3>
新株予約権の行使時に、社債による払い込み(代用払込)を要求する新株予約権付社債を言います。<br><br>会計方法としては、「区分法」と「一括法」が可能です。
<h3>ワラント債</h3>
新株予約権の行使時に、現金等による払込か社債による払い込みを選択可能な新株予約権付社債を言います。<br><br>会計方法としては、「区分法」のみ可能です。
