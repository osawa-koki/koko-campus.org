<h2>株主資本</h2>
純資産のうち、株主に帰属する部分です。<br>さらに以下の5つの区分に分けられます。<br>
<ul>
	<li>資本金</li>
	<li>新株予約権証拠金</li>
	<li>資本剰余金</li>
	<li>利益剰余金</li>
	<li>自己株式</li>
</ul>
<h2>新株の発行</h2>
株式の発行に際して<strong>申込証拠金</strong>を受け取った際は、その時点では誰に対して発行するかは未確定なので資本金としては処理しません。<br>また、受け取った申込証拠金も返す可能性もあり、当座預金と区別するため<strong>別段預金</strong>として処理します。
<div class="t">
	!申込証拠金受取り時!
	別段預金100,新株式申込証拠金100
</div>
実際に株式を発行する対象を決めた際には、<span class="u">新株式申込証拠金を資本金に振り替える</span>と同時に、<span class="u">別段預金を当座預金に振り替える</span>処理をします。<br><br><span class="u">資本金への振り替えに関しては、原則として払込金額の全額を資本金とする必要がありますが、払込金額のうち「２分の１」までは資本金とせずに<strong>資本準備金</strong>とすることが認められています。</span>
<div class="t">
	!新株発行時の処理!
	新株式申込証拠金100,資本金50
	,資本準備金50
	当座預金100,別段預金100
</div>
<h2>利益の処分</h2>
会社は当期純利益を出資者である株主に配当として支払う必要があります。<br><br>しかしながら、会社の財政基盤の安定化のため、<span class="u">資本準備金と利益準備金の合計額が資本金の４分の１に達するまで、配当金の１０分の１を準備金として積み立てなければならない</span>と会社法で規定しています。
<div class="t">
	!配当時!
	繰越利益剰余金100,未払配当金700
	,利益準備金100
	,別途積立金200
</div>
<h2>自己株買い</h2>
平成6年に商法が改正されたことで、会社が自社の株式を購入することが可能になりました。<br><br>自己株買いの主な目的は、「M&A対策」や「株価対策」ですが、市場に流通する株式が減るという観点から剰余金の配当と同様の効果を有するため、今日では配当の代わりに自己株買いをする企業も多数存在します。<br><br><br>自己株式を取得した際には<strong>取得原価</strong>で<strong>自己株式</strong>として処理します。<br>※自己株式の取得にかかる費用は<span class="u">取得原価に含めずに</span>支払手数料で処理します。
<div class="t">
	!自己株式購入時!
	自己株式200,当座預金190
	支払手数料10,
</div>
また、決算時に所有する自己株式に関しては評価替えをしません。
<h2>自己株式の取扱い</h2>
取得した自己株式に関して、企業は以下の2つの処理を選択することができます。<br>
<ul>
	<li>処分</li>
	<li>消却</li>
</ul><br>
<h3>自己株式の処分</h3>
自己株式の処分とは、自己株式を第三者に売却することを言います。<br><br>自己株式の処分時には、自己株式の帳簿価額を減額すると同時に処分対価との差額を<strong>自己株式処分差損益(その他資本剰余金)</strong>とします。
<h3>自己株式の消却</h3>
自己株式の消却とは自己株式を消滅させることを言います。<br><br>自己株式の消却時には、自己株式の帳簿価額を<strong>その他資本剰余金</strong>から減額します。
<div class="t">
	!自己株式消却時!
	その他資本剰余金30,自己株式30
</div>
<h2>分配可能額の算定</h2>
会社法では債権者程の観点から、配当可能金額を設定しています。<br><br>分配可能金額は、剰余金(その他資本剰余金 + 繰越利益剰余金 + etc...)から以下の4つを差し引いて算出します。<br>
<ul>
	<li>分配時の自己株式の帳簿価額</li>
	<li>前期末から分配時までの自己株式の処分対価</li>
	<li>剰余金から控除するのれん等調整額</li>
	<li>その他有価証券評価差額金(マイナス時)</li>
</ul><br>
<span class="r">
