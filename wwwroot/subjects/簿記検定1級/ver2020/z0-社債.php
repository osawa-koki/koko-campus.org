<div class="t">
	!クーポン利息の計上!
	@「額面金額 &times; クーポン利子率」をクーポン利息として計上します。<br>(利息法と同様)@
	社債利息20, 現金20
</div>
<div class="t">
	!金利調整差額の調整!
	@<strong><span class="ib nowrap"><span>「金利調整差額 &times; </span><span class="fraction"><span class="fraction_n">当期利用金額</span><span class="fraction_d">総利用金額</span></span></span>」</strong>を金利調整差額として社債の帳簿価額を調整します。@
	社債利息30,社債30
</div>
1年以内に償還される社債は<strong>一年以内償還社債</strong>に振り替えます。
