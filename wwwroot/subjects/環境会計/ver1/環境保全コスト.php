また、『環境報告ガイドライン2005』p.8においては「本ガイドラインで取り扱う環境保全コストは、企業等が環境保全のために負担したコスト（私的コスト）です。企業等の事業活動の結果、第三者や社会全体が被っている健康被害や環境汚染等の負担（社会的コスト）は対象としていません。」と書かれています。<br />換言すれば、企業が支出したものだけを環境保全コストの対象としているということになります。
