<h2>環境保全コスト</h2>
『環境報告ガイドライン2005』では環境会計の構成要素として以下の3つがあげられています。
<ul>
	<li>環境保全コスト</li>
	<li>環境保全効果</li>
	<li>環境保全対策に伴う経済効果</li>
</ul>
ここではそのうちのひとつである「環境保全コスト」について説明します。
<h2>環境保全コストの定義</h2>
環境保全コストとは「環境保全活動のための投資額及び費用額」と定義され、貨幣単位で測定するとしています。<br />また、環境保全活動とは「環境負荷の発生の防止、抑制又は回避、影響の除去、発生した被害の回復、又はこれらに資する取組」と定義されています。
<h2>環境保全コストの範囲</h2>
環境保全コストの範囲に関して『環境報告ガイドライン2005』では「環境保全目的で投下されたコストかどうかの判断は、目的基準によって判断されます。」と書かれています。<br />しかしばがら、実際には環境保全コストは他の目的と複合して発生している場合も多く、その場合は「差額集計」ないしは「按分集計」などの手法をもって捉えられます。<br /><br />また、環境保全コストは大きく「投資額」と「費用」からなり、「投資額」に関しては減価償却の考え方が適用され、「費用」に関しては発生主義の考え方が適用されます。
<h2>環境保全コストの分類</h2>
環境保全コストの分類に関して、『環境報告ガイドライン2005』では以下のように定められています。
