<h2>環境問題の提起</h2>
最初に環境問題を提起した有名な出来事を見ていきましょう♪
<h3>マルサス『⼈⼝論』</h3>
人口は幾何級数的に増加するのに対して食糧は算術級数的にしか増加しないことを指摘して、自然の状態では過剰人口による食糧不⾜は不可避とし、貧困は死亡率を⾼める積極的要因となり悪徳は出生率を低める予防的要因となるので過剰人口の抑⽌⼒として是認されると主張しました。
<h3>レイチェル・カーソン『沈黙の春』</h3>
1962年に出版され、殺虫剤等に使われるDDT等の化学物質は生物によって分解されないため、食物連鎖の上位の動物になればなるほど生物体内に⾼濃度で蓄積されることを指摘しました。
<h3>宇宙船地球号</h3>
1965年国連経済社会理事会であったアメリカの国連大使アドレイ・スティブンソンが「私たちは、全員が共に小さな宇宙船に乗って旅⾏している乗客で、わずかな空気と土に依存している」と述べ、環境問題に対する世界規模での連携の必要性を指摘しました。
<h3>ローマクラブ『成長の限界』</h3>
今後も現在(1972年)と同じような人口の爆発的増加と、経済成長が続いた場合、人口、食糧生産、生産資源、環境などを総合的に検討すると、100年以内に人類の成長は限界に達することを指摘しました。
