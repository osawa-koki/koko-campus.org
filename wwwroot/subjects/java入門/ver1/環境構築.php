「ツール&gt;オプション&gt;テキストエディター&gt;全般」から行間隔を変更します。<br />オススメは「1.15」です。
<h3>空白文字の表示</h3>
行間隔の変更画面と同じ、「ツール&gt;オプション&gt;テキストエディター&gt;全般」から変更します。<br />空白の表示にチェックを入れてください。
