<h2>C言語とは</h2>
<div class="bg-logo-c入門">
	プログラミング言語の基本中の基本の言語です。<br /><br />1972年にAT&T社のベル研究所で開発されて以来、システムやプログラムの基盤となる部分に用いられている言語です。<br /><br />「windows」「unix」「linux」などの主要なOSなどや組込み系OSなどで用いられています。<br />OSだけでなくアプリケーションの多くがc言語ないしはc言語の派生形であるc++言語で書かれています。<br /><br />特徴としては習得に時間がかかりますが、高い汎用性と実行効率を持つことが挙げられます。
</div>
<h2>コンパイルとビルド</h2>
コンピュータは我々が書いた英語みたいなコードを直接実行することはできません。<br />コンピュータが実行することができるのは「0」と「1」だけで書かれたバイナリデータと呼ばれるものだけです。<br />そのため、我々が書いたコード(ソースコード)をコンピュータが実行できるように変換する必要があります。<br />これを「<strong>コンパイル</strong>」ないしは「<strong>ビルド</strong>」と呼びます。
<img src="../pics/コンパイル.png" alt="コンパイルの説明画像" />
<h3>コンパイル</h3>
プログラミングでは、英語っぽいコード(<strong>ソースコード</strong>)を入力しますがコンピュータはこのコードを直接実行することはできません。<br /><br />コンピュータが我々が書いたソースコードを実行するためには一度ソースコードをコンピュータが実行できるコード(<strong>実行可能プログラム</strong>)へ変換する必要があります。<br /><br />この作業を「<strong>コンパイル</strong>」と呼びます。
<h3>ビルド</h3>
コンパイルの進化バージョンです。<br />入力されたコードをチェックして、さらには実行時に必要なライブラリを結合させて実行可能なプログラムを作成することを言います。<br />ここでは、コンパイルとビルドを厳密に区別することはしませんが、これ以降にビルドと言ったら「コンパイル + α」の作業だと捉えてください。
<h2>コンパイラの取得</h2>
c言語でプログラミングをする際にはまずc言語のコンパイラ(コンパイルをしてくれるプログラム)を取得する必要があります。<br /><br />c言語コンパイラの入手方法を以下の記載します。
<br />
<ul>
	<li>MinGW</li>
	<li>clang</li>
	<li>gcc</li>
</ul>
<br />
といっても、c言語のコンパイラを取得してそれをシェルでコマンドを打って、コンパイルetc...は大変ですので、ここではマイクロソフトが提供しているIDE(統合開発環境)であり、c言語等のコンパイラがプラグインで使用可能で簡単に実行可能な「<strong>visual studio</strong>」の使用を推奨します。
