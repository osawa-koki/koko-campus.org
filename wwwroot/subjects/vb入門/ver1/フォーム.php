<h2>イベント駆動</h2>
デスクトップアプリでは、イベント駆動で処理が実行されます。<br />イベントとは、ユーザのイベント(クリック・候補の選択・文字の入力・etc...)のことで、これを受けて定められた処理が実行されます。<br /><br />イベント駆動で実行される処理は以下のように記述します。
<code class="vb-dot-net">
	Public Class フォーム名
		Private Sub 関数名() Handles コントロール名.イベント名
			'処理...
		End Sub
	End Class
</code>
いちいち書くのは大変ですよね、、、<br />フォームのコード編集画面の上の表示されているコントロール一覧から対象のコントロールを選択して、右側のイベント一覧から選択したコントロールに登録するイベントを選択します。
<img src="../pics/イベントの登録.gif" alt="イベントの登録" />
これで自動で関数が作成されるので、ここに処理を書きます♪<br /><br />ここでは最も一般的なイベントであるクリックをボタンに登録します。<br /><br />この方法だと関数名の後に括弧に「sender As Object, e As EventArgs」と書かれていますが、これは無視してok!です。<br />削除してもok!ですが、、、
<div class="separator"></div>
では、これを使用してボタンがクリックされたら「ok!」と表示する簡単なプログラムを作成してみましょう♪
<code class="vb-dot-net">
	Public Class From1
		Private Sub hello() Handles Button1.Click
			Label1.Text = "Hello World!!!"
		End Sub
	End Class
</code>
ボタンとラベルのオブジェクト名はそれぞれ「Button1」と「Label1」に設定しています。<br />中のコードは気にしなくてok!です。
