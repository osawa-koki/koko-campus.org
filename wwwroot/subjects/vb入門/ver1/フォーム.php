<h2>フォーム</h2>
vb.netはデスクトップアプリが簡単に開発できるのが強みです。<br />他の言語では、外部のパッケージを利用してプログラミングする必要がありますが、vb.netではマウス操作で直感的にデスクトップアプリを開発することができます。<br /><br />vb.netではキーボードで操作するコンソールアプリケーションも可能ですが、これはプログラミングしていて楽しくないのでここでは説明しません。<br />書き方は同じですので、この授業を一通り学習すれば簡単に作れるようになります。
