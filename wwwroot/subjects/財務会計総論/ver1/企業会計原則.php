<h2>会計公準</h2>
会計基準を制定するにあたって必要となる、会計理論を構造するための基礎となる前提を言います。
<p class="r">公準とは公理のようなもので、理論体系を演繹展開するための基礎です。</p>
<img src="../pics/会計公準.png" alt="会計公準" />
もっとも代表的な会計公準としてギルマンの3公準があり、これは以下の3つの公準から成り立ちます。
<ul>
	<li>企業実体の公準</li>
	<li>継続企業の公準</li>
	<li>貨幣的評価の公準</li>
</ul>
<h3>企業実体の公準</h3>
会計単位の公準とも呼ばれ、会計記録対象を場所的に限定します。<br />具体的には会計の行われる範囲を企業の経済活動のみに限定し、株主を企業から独立した存在とみなします。
<img src="../pics/企業実体の公準.png" alt="企業実体の公準" />
<h3>継続企業の公準</h3>
会計期間の公準とも呼ばれ、会計記録対象を時間的に限定します。<br />具体的には企業は半永久的に経営活動を継続すると仮定し、その全経営期間を一定の会計期間に区切って、毎期財務諸表を作成して、財政状態・経営成績を報告します。
<p class="r">従来はある目的を達成したら企業を解散することとが多く、倒産企業の前提を採用していました。</p>
例えば、以下の会計処理が継続企業の公準を採用していることを如実に表しています。
<ul>
	<li>資産の減価償却</li>
	<li>取得原価主義</li>
	<li>費用配分の原則</li>
</ul>
<img src="../pics/継続企業の公準.png" alt="継続企業の公準" />
<h3>貨幣的評価の公準</h3>
会計記録対象を内容的に限定します。<br />すべての会計事実を貨幣によって捉えるとする公準です。<br />これによって会計の対象が貨幣により評価可能なものに限定されます。<br />例えば、ポテンシャルなどの貨幣によって評価できないものは財務諸表に計上しません。
<img src="../pics/貨幣的評価の公準.png" alt="貨幣的評価の公準" />
<h2>企業会計原則</h2>
企業会計原則とは昭和24年に制定された財務諸表作成のための理論的な土台を提供するものです。<br />あくまでの理論的な土台を提供するものであって、現在においてすべて適用されているわけではありません。<br />また、全ての会計事象を網羅しているわけではなく、新しい会計事象等に関しては独自の会計基準を制定することででこれを補っています。<br /><br />財務諸表は「記録と慣習と判断の総合表現」とも呼ばれ、その作成に当たっては経営者の主観性が強く混入します。<br />したがって、経営者の恣意性によって会計情報が信頼できないものになってしまうため、これに制約を課すという観点から企業会計原則が必要とされています。
<p>このような社会的な規範として形成された会計原則はGAAP(一般に公正妥当と認められる企業会計の基準)と呼ばれます。</p>
企業会計原則は以下の3つから構成されています。
<ul>
	<li>一般原則</li>
	<li>貸借対照表原則</li>
	<li>損益計算書原則</li>
</ul>
<img src="../pics/企業会計原則.png" alt="企業会計原則" />
<h2>一般原則</h2>
企業会計原則の一般原則は以下の7(+1)つから構成されます。
<ol>
	<li>真実性の原則</li>
	<li>正規の簿記の原則</li>
	<li>資本取引・損益取引区分の原則</li>
	<li>明瞭性の原則</li>
	<li>継続性の原則</li>
	<li>保守主義の原則</li>
	<li>単一性の原則</li>
	<li>(重要性の原則)</li>
</ol>
<h3>真実性の原則</h3>
<div class="quote">
	<div>企業会計原則 第一 一</div>
	企業会計は、企業の財政状態及び経営成績に関して、真実な報告を提供するものでなければならない。
</div>
一般原則の最高規範であり、企業会計における究極的な目標を示しています。<br />企業会計は企業の財政状態・経営成績に関して、真実な報告を提供することを要求する原則です。<br />ここでいう真実とは<strong>相対的な真実</strong>を指します。<br />理由は、会計はその目的に依存するため相対的にならざるを得ないこと、また半永久的に継続することを強制的に会計期間に分割することが事実上不可能であること、ひとつの会計事象に対して複数の代替的会計処理が容認されていることがあげられます。
<h3>正規の簿記の原則</h3>
<div class="quote">
	<div>企業会計原則 第一 二</div>
	企業会計は、すべての取引につき、正規の簿記の原則に従って、正確な会計帳簿を作成しなければならない。
</div>
会計事象を網羅性・検証可能性・秩序性を維持した状態で把握して会計帳簿を作成すること、さらにその会計帳簿から財務諸表を作成する際に両者間において有機的な関連性を維持することを要求します。
<img src="../pics/正規の簿記の原則.png" alt="正規の簿記の原則" />
<h4>網羅性</h4>
利害関係者が求める会計情報を開示するにあたって、企業の財政状態・経営成績・キャッシュフローの状況に影響を与える全ての取引を含んでいることを要求します。
<img src="../pics/正規の簿記の原則(網羅性).png" alt="正規の簿記の原則" />
<div class="separator"></div>
網羅性の観点からは全ての会計事象を会計帳簿に記録する必要があり、簿外資産・簿外負債(会計帳簿に記録しない資産・負債)の存在を認めていませんが、重要性の原則によってこれが認められこともあります。
<h4>検証可能性</h4>
会計帳簿から遡って会計取引の存在を立証できることを要求します。
<img src="../pics/正規の簿記の原則(検証可能性).png" alt="正規の簿記の原則" />
<h4>秩序性</h4>
会計帳簿(仕訳帳・元帳)の会計記録が体系的であって、相互関係性が保たれていることを要求します。
<img src="../pics/正規の簿記の原則(秩序性).png" alt="正規の簿記の原則" />
<h3>資本取引・損益取引区分の原則</h3>
<div class="quote">
	<div>企業会計原則 第一 三</div>
	資本取引と損益取引とを明瞭に区別し、特に資本剰余金と利益剰余金とを混同してはならない。
</div>
資本取引と損益取引との区分は以下の2つに分割できます。
<ul>
	<li>資本取引と損益取引の区分</li>
	<li>資本剰余金と利益剰余金の区分</li>
</ul>
<h4>資本取引と損益取引の区分</h4>
資本取引とは主たる企業活動とは無関係に発生する取引を指し、これは純資産を直接的に増減させます。<br />これに対して、損益取引とは主たる企業活動に関わって発生する取引を指し、純資産には影響を与えません。
<div class="separator"></div>
資本取引と損益取引の区分を要求する理由は情報提供機能の観点から説明できます。<br />財務諸表利用者が想定するのは期間損益であるのに対して、損益には関係ない、例えば株式の新規発行によって得た収入を利益として記録すると、利用者の判断を誤らせる危険性があります。<br />そこで、企業会計原則では資本取引と損益取引の区分を要求しています。
<h4>資本剰余金と利益剰余金の区分</h4>
資本剰余金とは資本取引から生じた元本である資本のうち、資本金以外を指し、これはさらに資本準備金と資本剰余金に分類されます。<br />これに対して、利益剰余金とは村営取引から生じた利益であり、これはさらに利益準備金と利益剰余金に分類されます。
<div class="separator"></div>
資本剰余金と利益剰余金の区分を要求する理由は利害調整機能の観点から説明できます。<br />株主・債権者間の利害対立に関して、元本部分(資本剰余金)を分配不可とし、留保利益を分配可能とすることで株主と債権者の利害調整を実現しています。
<p class="r">理論としては資本剰余金と利益剰余金で分配可能か否かを判定することが適切ですが、現行制度では準備金と剰余金で分配可能か否かを判定しています。</p>
