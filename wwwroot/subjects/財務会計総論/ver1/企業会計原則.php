<h3>継続性の原則</h3>
<div class="quote">
	<div>企業会計原則 第一 五</div>
	企業会計は、その処理の原則及び手続を毎期継続して適用し、みだりにこれを変更してはならない。
</div>
原則として企業には経理自由の原則が適用され、ひとつの会計事象に対して複数の代替的な会計処理が認められます。<br />これに関して継続性の原則では、一度採用した会計方針を正当な理由なく変更せず、<strong>正当な理由がない限りは</strong>各期を通じて継続して適用することを要求します。<br />会計方針のみだりな変更を許すと、経営者が恣意的な会計処理を行うことで利益操作に用いられる危険性があります。<br />さらには同一の会計処理を継続して適用することで各期の会計情報の比較可能性が保証されます。
<p class="r">比較可能性については遡及処理を行うことで、会計処理を変更しても保証されるという主張もあります。</p>
継続性の原則に関しては、あくまでも認められた会計手法から認められた会計手法への変更時にのみ適用され、認められない会計手法への変更、ないしは、認められない会計手法から認められた会計手法への変更には適用されません。
<img src="../pics/継続性の原則.png" alt="継続性の原則" />
<h3>保守主義の原則</h3>
<div class="quote">
	<div>企業会計原則 第一 六</div>
	企業の財政に不利な影響を及ぼす可能性がある場合には、これに備えて適当に健全な会計処理をしなければならない。
</div>
「未実現の費用はこれを計上し、未実現の収益はこれを計上しない」という会計理論の格言によく現れています。<br />第一の企業会計原則で「真実性の原則」を学び、そこでいう真実とは相対的な真実であることも学習しましたね♪<br />会計処理に関しては不確実性が大きく介入し、結果的に算定した会計情報と異なる結果になることも想定されます。<br />利益として計上した額が不確実性が明らかになった時点で利益ではなくなったり、、、<br />そこで、保守主義の原則ではこのような不確実性に対して、費用は大きく収益は小さく処理することで結果的に利益を小さく把握することで、安定した会計情報を作成します。<br />これはあくまでも真実性の原則の範囲内で行うべきで、過度な保守主義は認められません。
<img src="../pics/保守主義の原則.png" alt="保守主義の原則" />
<h3>単一性の原則</h3>
<div class="quote">
	<div>企業会計原則 第一 七</div>
	株主総会提出のため、信用目的のため、租税目的のため等種々の目的のために異なる形式の財務諸表を作成する必要がある場合、それらの内容は、信頼しうる会計記録に基づいて作成されたものであって、政策の考慮のために事実の真実な表示をゆがめてはならない。
</div>
二重帳簿を禁止し、財務諸表の形式的多元性を求めながらも、実質的一元を要求します。<br />会計報告(財務諸表)は目的によって使い分けることが想定されますが、この財務諸表を作成する際に使用する帳簿は一元である必要があります。
<img src="../pics/単一性の原則.png" alt="単一性の原則" />
<div class="separator"></div>
<h3>重要性の原則</h3>
<div class="quote">
	<div>企業会計原則 注一</div>
	企業会計は、定められた会計処理の方法に従って正確な計算を行うべきものであるが、企業会計が目的とするところは、企業の財務内容を明らかにし、企業の状況に関する利害関係者の判断を誤らせないようにすることにあるから、重要性の乏しいものについては、本来の厳密な会計処理によらないで他の簡便な方法によることも正規の簿記の原則に従った処理として認められる。<br />
	重要性の原則は、財務諸表の表示に関しても適用される。
