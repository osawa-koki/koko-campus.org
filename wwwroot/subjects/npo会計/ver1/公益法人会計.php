<h2>公益法人会計(正味財産増減計算書)</h2>
企業会計でいう、損益計算書に該当します。<br />まずは公益法人会計における正味財産増減計算書を見てみましょう♪
<img class="no-max" src="../pics/公益法人会計(正味財産増減計算書).png" alt="公益法人会計" />
<p class="r"><a href="http://kohokyo.or.jp/jaco40/disclosure/">公益法人 公益法人協会</a>より引用。</p>
<p class="r">(事業費)と(管理費)は僕が付け加えました。</p>
正味財産増減計算書ではまず、一般正味財産増減の部と指定正味財産増減の部に区分します。<br />そのうち、一般正味財産増減の部は経常増減の部と計上外増減の部に分類され、さらに経常増減の内訳として、経常費用は事業費と管理費に分けられます。<br /><br />企業会計での損益計算書における最終的な利益を示す当期純利益は正味財産増減計算書においては当期一般正味財産増減額として表されます。
<h3>正味財産の振替</h3>
先ほどは公益法人会計における貸方・借方の項目間で以下の2つの紐付の制限について説明した際に「もっとも、お金に色はついてないのでこれを正確に判断することはできませんが、、、」と書きましたが、この紐付けに関しては正味財産増減計算書を一般正味財産増減の部と指定正味財産増減の部に区分することで実現しています。<br />また、一般正味財産増減の部と指定正味財産増減の部間で直接のやり取りがあると、紐付け制限を回避されることがあるので、両者間のやり取り(指定正味財産から一般正味財産への振替)は最終的に算出された指定正味財産増減額を一般正味財産増減額に振り替えることで紐付けの状況を明確にします。
<p class="r">一般正味財産から指定正味財産への振替は想定しません。<br />理由は使途に制限がない一般正味財産を使途に制限がある指定正味財産に振り替える利点がないからです。</p>
<h2>公益法人の税制優遇</h2>
公益法人認定されると、当該法人は税制上の優遇を受けることだできるだけなく、企業が当該法人に寄付する際にも税制上の優遇を受けることができます。
<p class="r">国・都道府県公式公益法人行政総合情報サイトは<a href="https://www.koeki-info.go.jp/contribute/kihu-zeisei_n5.html">こちら</a>。</p>
公益法人は税制上の優遇を受けることができるため、その認定制度が正しく機能しないと税の不公平を生む危険性があります。<br />そのため公益法人は以下の要件を満たす必要があります。
<ul>
	<li>公益目的事業比率50%以上</li>
	<li>収支相償</li>
	<li>遊休財産制限</li>
	<li>理事・幹事の独占・寡占制限</li>
	<li>経理基盤の確保</li>
	<li>一般への情報公開</li>
</ul>
また、公益法人が作成した計算書類(貸借対照表・正味財産増減計算書)と財産目録、CF計算書、事業報告書、付属名明細書は幹事監査を受ける必要があります。<br />さらには、公益法人は原則として会計監査人を設置する必要があります。
