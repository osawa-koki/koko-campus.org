企業会計と大きく異なる点は、貸方・借方の項目間の紐づけ可否が設定されていることです。<br />企業会計では資金の調達源泉が自己資本(純資産)であれ他人資本(負債)、その使用先に制限はありません。
<p class="r">資本金維持のための制限を除く。</p>
公益法人会計では貸方・借方の項目間で以下の2つの紐付が制限されています。
<ul>
	<li>負債で基本財産を購入することの禁止</li>
	<li>指定正味財産でその他固定資産を購入することの禁止</li>
</ul>
