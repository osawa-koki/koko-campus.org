<h2>公益法人会計の基本思考</h2>
公益法人会計に関しては以下の4つの基本思考があります。
<ul>
	<li>企業会計の導入</li>
	<li>計算書類の簡素化</li>
	<li>受託責任の明示</li>
	<li>収支相償</li>
</ul>
<h3>企業会計の導入</h3>
公益法人会計においては原則として企業会計と同様の手法を用います。<br />例えば資産に関しては減価償却を要求し、有価証券に関しては期末時価での評価を要求しています。
<h3>計算書類の簡素化</h3>
NPOが企業と同様の厳格な会計基準を遵守することは困難であると想定できるため、会計帳簿・収支予算書・収支計算書に関しては報告を不要としています。
<p class="r">大規模法人にはCF計算書の作成を義務付けています。</p>
<h3>受託責任の明示</h3>
正味資産の二区分と対応資産の明示を要求しています。
<h3>収支相償</h3>
公益目的事業収入がそれを実施する際にかかる費用を大幅に超えてはいけないという要件があるため、公益法人会計ではこれを把握できるように調整する必要があります。
