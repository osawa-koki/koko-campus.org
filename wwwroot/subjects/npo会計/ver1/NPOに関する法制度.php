<h2>NPO法人</h2>
平成10年に施行された特定非営利活動促進法(NPO法人法)によって規定された法人を指し、ボランティア活動をはじめとする市民が行う自由な社会貢献活動としての特定非営利活動の健全な発展を促進し、公益の増進に寄与することを目的とする法人です。
<div class="quote">
	<div>特定非営利活動促進法 第一条</div>
	この法律は、特定非営利活動を行う団体に法人格を付与すること並びに運営組織及び事業活動が適正であって公益の増進に資する特定非営利活動法人の認定に係る制度を設けること等により、ボランティア活動をはじめとする市民が行う自由な社会貢献活動としての特定非営利活動の健全な発展を促進し、もって公益の増進に寄与することを目的とする。
</div>
また、第二条では特定非営利活動を主たる目的とすると定められ、さらには政治活動・宗教活動を禁止しています。
<div class="quote">
	<div>特定非営利活動促進法 第二条 (抜粋)</div>
	この法律において「特定非営利活動」とは、別表に掲げる活動に該当する活動であって、不特定かつ多数のものの利益の増進に寄与することを目的とするものをいう。<br />
	２　この法律において「特定非営利活動法人」とは、特定非営利活動を行うことを主たる目的とし、次の各号のいずれにも該当する団体であって、この法律の定めるところにより設立された法人をいう。<br />
	一　次のいずれにも該当する団体であって、営利を目的としないものであること。<br />
	イ　社員の資格の得喪に関して、不当な条件を付さないこと。<br />
	ロ　役員のうち報酬を受ける者の数が、役員総数の三分の一以下であること。<br />
	二　その行う活動が次のいずれにも該当する団体であること。<br />
	イ　宗教の教義を広め、儀式行事を行い、及び信者を教化育成することを主たる目的とするものでないこと。<br />
	ロ　政治上の主義を推進し、支持し、又はこれに反対することを主たる目的とするものでないこと。<br />
	ハ　特定の公職（公職選挙法（昭和二十五年法律第百号）第三条に規定する公職をいう。以下同じ。）の候補者（当該候補者になろうとする者を含む。以下同じ。）若しくは公職にある者又は政党を推薦し、支持し、又はこれらに反対することを目的とするものでないこと。
</div>
