<h2>認定NPO制度</h2>
一般法人がある要件を満たせば公益法人になり税制上の優遇を受けられるのと同様に、NPO法人もある要件を満たせば認定NPO法人になり税制上の優遇を受けられます。<br />認定NPOになるための判定指標として以下の3つがあります。
<ul>
	<li>適切な組織運営</li>
	<li>適切な情報開示</li>
	<li>広い一般市民からの支持</li>
</ul>
<h3>適切な組織運営</h3>
役員のうち親族が三分の一以下であることや、設立後一定期間が経過していること、会員による共益的活動が50%以下であることが求められます。
<h3>適切な情報開示</h3>
貸借対照表・事業報告書・活動計算書・明細書が適切な経理に基づいて作成され、適切に開示されていることが求められます。
<h3>広い一般市民からの支持</h3>
一般市民からの広い支持があるかどうかの判定にはPST(パブリック・サポート・テスト)によって行われます。<br />これは収入のうちの一定割合以上を寄付金が占めることを確認します。<br />PSTには相対的指標と絶対的指標の2種類があります。
