<h2>NPO法の意義</h2>
NPO法は平成10年に施行された新しい法律です。<br />ここではなぜ法制度が改革されたのか、どのように変わったのかを学びましょう♪
<h3>改革点</h3>
従来は法人の設立には主務官庁の許認可が必要で、その公益性の判定は主務官庁が行っていました。<br />換言すれば、主務官庁が絶対的な権力を持ち、NPOは全て行政の指揮監督下にあったと言えます。<br /><br />改革後は設立は登記のみで可能となり、公益性の判定の基準が明確化されました。<br />その代わりにNPOに対する情報公開の必要性が強調されました。
<h3>なぜ改革されたか</h3>
NPOは公共性の高い活動を営むことが期待されますが、これに関して一般市民ではなく行政が関与しすぎるとその目的を達成できないという問題が存在したからです。<br />公共性に関しては行政ではなく、その恩恵を受ける一般市民自体が判断するべきという考え方に基づいています。<br />そのため、一般市民によるNPOのチェック体制を整えるためにNPOに対して適切な情報公開を要求しています。<br />NPO法ではNPOが行うべき会計処理について規定しています。
<div class="quote">
	<div>特定非営利活動促進法 第二十七条</div>
	特定非営利活動法人の会計は、この法律に定めるもののほか、次に掲げる原則に従って、行わなければならない。<br />
	一　削除<br />
	二　会計簿は、正規の簿記の原則に従って正しく記帳すること。<br />
	三　計算書類（活動計算書及び貸借対照表をいう。次条第一項において同じ。）及び財産目録は、会計簿に基づいて活動に係る事業の実績及び財政状態に関する真実な内容を明瞭に表示したものとすること。<br />
	四　採用する会計処理の基準及び手続については、毎事業年度継続して適用し、みだりにこれを変更しないこと。<br />
</div>
これは企業会計原則のうちの「正規の簿記の原則」「明瞭性の原則」「継続性の原則」に該当します。<br />因みに第一項には「予算準拠原則」が規定されていましたが削除されました。<br />これはNPOは柔軟性のある活動を行うことが想定されるため、予算の編成・遵守が困難であるからです。<br /><br />しかしながら、具体的な会計手法に関しては統一されておらず、各NPOに委ねられています。
