<h2>製造工業原価計算要綱の目的</h2>
製造工業原価計算要綱が制定される以前は海軍と陸軍で異なる原価計算制度を使用していました。<br /><br />しかしながら、陸軍と海軍で異なる原価計算制度を使用することは陸軍と海軍の両方に物資を納入している工場事業場などでの処理が複雑化する他、陸軍と海軍双方を用いる戦争では不都合でした。<br /><br />また、戦争中により強力な価格統制のため、民間向上に関しても原価計算制度を使用させる必要がありました。
