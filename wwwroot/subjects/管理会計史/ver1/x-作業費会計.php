<h2>殖産興業と官営工場</h2>
殖産興業では明治政府が主体となって工業の推進に取り組みました。<br />民間ではなくて政府が立てた工場を官営工場(政府作業場)と言いますが、明治政府はこの官営工場を多数建設しました。<br /><br />官営富岡製糸場など中学の社会の授業で学んだ人も多いと思います。<br /><br />官営工場での会計では前の授業で説明した大蔵省が制定した一連の出納規定が適用されましたが、増加する支出を削減するためには根本的な制度改革が必要とされました。
<h2>作業費会計</h2>
