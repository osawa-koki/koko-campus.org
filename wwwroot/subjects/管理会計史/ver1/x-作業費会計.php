<h2>作業費区分及受払例則</h2>
明治政府の一般経費から作業費を分離独立させることで、各官営工場の損益を計算する試みが行われました。<br /><br />この例則の特徴は以下の3つです。
<br />
<ol>
	<li>作業費を一般経費から分離</li>
	<li>作業費を興業費と営業費に分類</li>
	<li>収益と費用の対応(期間損益計算)</li>
</ol>
<br />
「1」に関しては、前述のとおり官営工場の独立採算を図っているため、官営工場の会計を明治政府の一般経費会計から切り離すことを規定しています。<br /><br />「2」に関しては、その性質によって費用を分類することを規定しています。<br /><br />「3」に関しては、物品の購入時に支出とするのではなく、消費時に支出として認識するなど期間損益計算を規定してはいたものの、償却に関しては具体的な処理方法は示されず、不十分な規定であったと言えます。
