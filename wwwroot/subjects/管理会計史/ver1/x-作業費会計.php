</div>
<h2>作業費出納條例</h2>
「作業費区分及受払例則」の最大も問題点は償却方法の規定が不十分であったことでした。<br />作業費出納條例では興業費・営業費の償却が詳細に規定されます。<br /><br />興業費・営業費の償却を規定したことで、固定資産（資本）の減価とそれの製品への価値移転の認識を規定することとなり、間接費と直接費の区分を中心とする現在の原価計算の礎を築いたと言えます。<br /><br />また、減価を認識することで投下資本の回収分も同時に認識することとなり、これは継続企業の前提を確立したことになります。
<h2>改正作業費出納條例</h2>
作業費出納條例での興業費・営業費の分類は支出した時点の違いによるものでした。<br />しかしながらこの分類方法では本来興業費として計上すべき費用も計上時期が異なるだけで営業費として計上されてしまい、同様に、本来営業費として計上すべき費用も計上時期が異なるだけで興業費として計上されてしまうという問題がありました。<br /><br />これを克服するために、改正作業費出納條例では支出した時点ではなく支出の性格の違いから興業費と営業費を分類することとしました。<br /><br />この会計学的意義としては、作業費が初期投下資本と運転資本の区分から固定資本と営業資本の区分に変更されたことで費目別計算思考がより精緻化したことがあげられます。<br /><br />換言すると、収益と対応させるための費用として営業費のみが計上されることで期間損益の比較が可能になり、初期投下資本と事業運営費の区分がされました。
