また、一般管理費及販売費に関しても原価として集計していることは従来の規則とは大きく異なる点です。<br /><br />さらには「原価ニ算入スルコトヲ得ザル費用」として非原価項目についても言及している点では、現代の原価計算にかなり近い仕組みが整ったと言えます。
<h3>仕組み</h3>
「海軍軍需品工場事業場原価計算準則」では部門別計算に関しても規定していて、まず原価部門を製造部門と補助部門に分け、補助部門で発生した費用は適切な配賦基準で製造部門へ配賦することを規定しています。<br /><br />さらに間接費の各製造指図書への按分賦課は、定額によって行うとしています。
