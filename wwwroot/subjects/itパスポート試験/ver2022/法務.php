<h2>不正競争防止法</h2>
不正競争防止法とは、企業の活動にとって大切な秘密の情報を守り不正な競争を禁止する法律です。<br /><br />この法律は、企業の活動にとって大切な秘密の情報を<strong>営業秘密(トレードシークレット)</strong>として保護します。<br />営業秘密(トレードシークレット)として認定されるには以下の3つの要件を満たす必要があります。
<ul>
	<li>秘密として管理されており、</li>
	<li>事業活動に有益な情報で、</li>
	<li>公然と知られていないもの。</li>
</ul>
