<h3>産業財産権</h3>
産業財産権とは、<strong>知的財産権</strong>のひとつで<strong>特許権</strong>・<strong>実用新案権</strong>・<strong>意匠権</strong>・<strong>商標権</strong>のことです♪<br /><br />具体的な意義とは、<strong>産業の発展</strong>のために保護されるように設定された権利です。
<h4>特許権</h4>
特許権は産業財産権として保護される創作物に対する権利のうち、かなり高度なもの(<strong>発明</strong>)に対して設定されます。
<h4>実用新案権</h4>
実用新案権は産業財産権として保護される創作物に対する権利のうち、発明ほど高度でないもの(<strong>考案</strong>)に対して設定されます。
<h4>意匠権</h4>
意匠権は産業財産権として保護される創作物に対する権利のうち、デザイン・形・模様などに対して設定される権利です。
<h4>商標権</h4>
商標権は産業財産権として保護される創作物に対する権利のうち、商品に使用される文字(いわゆる<strong>トレードマーク</strong>)に対して設定されます。
