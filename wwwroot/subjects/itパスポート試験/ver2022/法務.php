<h3>PL法(製造物責任法)</h3>
企業が製品の欠陥によって、消費者に損害を与えた場合は原則として企業が消費者に対して損害賠償責任を帯びることを規定しています。<br />ソフトウェアの欠陥によりハードウェアが故障した場合は、ソフトウェアの作成企業はハードウェアの故障に関して賠償責任を負います。
<h3>特定電子メール法</h3>
不特定多数に対して広告メールを送信することを禁止する法律です。<br />広告メールを送信する場合には、事前に許可を得てかつ広告メール送信者の名称を表示しなければなりません。
