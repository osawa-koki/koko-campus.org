<h3>労働基準法(労働に関する法律)</h3>
労働者が雇用者を労働させるにあたって守るべきルールです。<br /><br />労働基準法では以下の内容が含まれています。
<ul>
	<li>労働時間は1週間で40時間、1日8時間。</li>
	<li>1日の労働時間が8時間を超える場合は1時間以上の休憩を与える。</li>
	<li>時間外の残業や休日出勤をさせる場合は労使協定を結ぶ必要あり。</li>
</ul>
<h3>労働派遣法</h3>
労働者の派遣とは「<strong>自分が雇用する労働者</strong>を、<strong>他人の指揮命令</strong>を受けて、その他人のために働かせる契約」のことを言います。
<img src="../pics/派遣.png" alt="派遣" />
<h4>請負契約</h4>
請負契約とは、請負業者が雇用している労働者を指揮しながら<strong>業務の完成</strong>を実行する契約を言います。<br /><br />注文主が請負業者に<strong>仕事の完成</strong>を任せるだけで、注文主が請負業者の労働者に直接命令することはできません。<br />注文主が請負業者の労働者に直接命令すると仮装請負となります。
<img src="../pics/請負.png" alt="請負契約">
<h4>委任契約</h4>
委任契約とは、委託企業が受託企業に一定の行為をすることを要求する契約を言います。<br /><br />請負契約との違いは受託企業は<strong>ベストを尽くす</strong>だけでよく、<strong>業務の完成は必ずしも求められない</strong>ことにあります。<br /><br />例えば、お医者さんと患者さんが結ぶのが委任契約です。<br />お医者さんとしては病気を絶対に治す契約なんて結べませんからね、、、
<p>正確には準委任契約とされます。</p>
<h3>個人情報保護法</h3>
個人情報とは個人に関する情報で、個人情報を取り扱う事業者は以下の事項を遵守する義務を帯びています。
<ul>
	<li>個人情報の利用目的を開示</li>
	<li>利用目的を超えた個人情報の利用は禁止</li>
	<li>本人の許可なくして第三者に個人情報の提供禁止</li>
</ul>
<h3>不正アクセス禁止法</h3>
<strong>「不正アクセス行為」</strong>と<strong>「不正アクセスを助長する行為」</strong>を禁止します。<br /><br />不正アクセス行為と不正アクセスを序要する行為とは以下の通りです。
<table>
	<caption>不正アクセス行為・不正アクセスを助長する行為</caption>
