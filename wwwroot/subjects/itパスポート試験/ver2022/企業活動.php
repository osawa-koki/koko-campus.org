<h4>仕訳帳</h4>
各取引ごとにその内容を記録します。
<h4>総勘定元帳</h4>
仕訳を各勘定ごとに合算します。
<h4>試算表</h4>
総勘定元帳を基に、各勘定科目の合計額・残高を計算します。
<h4>精算表</h4>
各勘定科目の残高を求めます。
<h4>財務諸表</h4>
最終的に企業の財政状態・経営成績等についてまとめます。<br />代表的なものには「貸借対照表」と「損益計算書」があります。
<h5>貸借対照表と損益計算書</h5>
ここでは「貸借対照表」と「損益計算書」と違いについて理解しましょう♪<br /><br /><strong>貸借対照表</strong>は<strong>ある時点</strong>における企業の<strong>財政状態</strong>を表します。<br /><strong>損益計算書</strong>は<strong>一定期間</strong>における企業の<strong>経営成績</strong>を表します。<br /><br />簡単にまとめてみました♪
