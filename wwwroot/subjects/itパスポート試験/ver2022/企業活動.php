Q.利益がゼロになる売上高を求めてください♪
<div class="separator"></div>
最初に変動費率を算出します。<br />「変動費 &divide; 売上高」より「0.5」となります。<br /><br />次に「固定費 &divide; (1 - 変動費率)」より損益分岐点売上高を算出します。<br />「200万 &divide; (1 - 0.5)」より「400万」となります。
<h4>原価の分類</h4>
先ほどは原価(費用)を変動費と固定費に分けましたね♪<br />これは操業度(ここでは販売数量)という指標により原価を分類しました。<br />ですけど原価ってそれ以外にもいろいろな分類ができるんです、、、<br />ここではそれ以外の分類方法を2つ覚えましょう♪
<h5>形態別分類</h5>
形態別分類では何を消費したのかによって原価を分類します。<br />形態別分類では以下の3つに分類できます。
<ul>
	<li>材料費</li>
	<li>労務費</li>
	<li>経費</li>
</ul>
比較的、イメージしやすいと思います。ちなみに、経費とは材料費・労務費に分類されない原価をひとくくりにして経費とします。
<h5>製品との関連による分類</h5>
製品との関連による分類ではその原価がどの製品に使われたか分かるかどうかで製造直接費と製造間接費に分類します。<br />例えば某ハンバーガー屋さんでのパンは何(どの製品)に使われたか分かるので製造直接費です。セットのポテトを揚げるための油はポテト以外の製品にも使われているので具体的に使用先を特定できないため製造間接費となります。
