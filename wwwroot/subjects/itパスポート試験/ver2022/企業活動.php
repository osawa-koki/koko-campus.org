<h4>損益分岐分析問題の解き方</h4>
上で単語については詳しく説明したんですけど、解き方については公式だけを簡単に説明しますね♪
<div class="quote">
	<div>損益分岐点売上高の算出</div>
	損益分岐点売上高(利益がゼロになる時の売上高) = 固定費 &divide; (1 - 変動費率)
	<p>変動費率は「変動費 &divide; 売上高」で求められます。</p>
</div>
