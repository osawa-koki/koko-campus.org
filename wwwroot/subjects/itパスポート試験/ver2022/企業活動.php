<h6>損益計算書</h6>
ある期間における会社の経営成績を明らかにします。<br />「利益 = 売上高 - (売上原価 + 販売費及び一般管理費 + 営業外費用(収益) + 特別損失(利益))」という損益計算書等式が必ず成立します。
<h3>管理会計</h3>
管理会計ってなんだか覚えていますか?<br /><br />復習しますね♪<br />管理会計とは企業内部の人(主に経営者・部長)に向けた会計です。<br /><br />ですから、財務諸表(財務会計)が事実を適切に報告する必要があるのに対して、管理会計では経営に際して有益な情報を提供する必要があるんです。<br /><br />「経営に際して有益な情報」ってなんやって思いますよね、、、<br />結論としては企業(経営者)によって異なります。ある企業が欲してるデータは他の企業からしたらいらなかったりと、、、<br /><br />ということでここではもっとも一般的なデータである損益分岐点について学びます。
