<h3>財務会計</h3>
企業の売上高や利益ってどのように算出されているのでしょうか???<br />ここでは企業が財務諸表(最終的に企業外部の人に報告するための資料)を作成するための手順について学びましょう♪<br />以下の手順を踏みます。
<ol>
	<li>仕訳帳</li>
	<li>総勘定元帳</li>
	<li>試算表</li>
	<li>精算表</li>
	<li>財務諸表</li>
