</table>
機器とディスプレイを結ぶためのインターフェースとして以下のものがあります。
<ul>
	<li>DVI</li>
	<li>HDMI</li>
</ul>
ディスプレイの解像度の単位にはピクセルが使用されます。<br />「縦 &times; 横」でディスプレイ全体の解像度が求められます。<br /><br />また、1ピクセルに色に関する情報をどれだけもたせるかによって1画面当たりの情報量が算出されます。<br />具体的には以下のように計算します。
<div class="quote">
	<div>1画面当たりの情報量</div>
	= ピクセル数(横) &times; ピクセル数(縦) &times; 1ピクセル当たりの情報量
</div>
