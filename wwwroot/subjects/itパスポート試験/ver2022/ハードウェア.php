<h3>ディスプレイ</h3>
ディスプレイはその性質から以下の2つに分類されます。
<table>
	<tbody>
		<tr>
			<th>液晶ディスプレイ</th>
			<td>液晶パネルを使用したディスプレイです。<br />液晶自体は発行しないので、バックライトを用いて表示します。</td>
