<h2>CPU</h2>
コンピュータの脳に該当する部分です。<br />制御機能と演算機能を担当します。<br /><br />代表的なCPUにはIntelのcoreシリーズとAMDのryzenシリーズがあります。
<h3>クロック周波数</h3>
CPUの性能を表す指標として使用されます。<br /><br />クロック信号と呼ばれる信号が1秒間に何回発せられるかを意味し、単位はHz(ヘルツ)です。<br />クロック周波数が高いほど高速に動作します。
<h2>マザーボードとバス</h2>
マザーボードとは、コンピュータの本体となる部分です。<br />マザーボードに部品を取り付けて動作させます。
<img src="../pics/マザーボード.webp" alt="マザーボード" />
上のマザーボードは<a href="https://www.asus.com/jp/Motherboards-Components/Motherboards/PRIME/PRIME-Z690-A/">ASUS社のPRIMEシリーズ</a>の画像です。<br /><br />また、マザーボードに取り付けられた装置間でやり取りをするための電子回路をバスと呼びます。
<h2>主記憶装置</h2>
メモリと呼ばれる装置が該当します。<br />正確にはメモリは「メインメモリ」と「キャッシュメモリ」に分類され、このうちの「メインメモリ」を主記憶装置と呼びますが、現在ではこれらはほとんど使い分けられません。<br /><br />メモリには「ROM」と「RAM」の2種類があります。
<h3>ROM</h3>
「Read Only Memory」の略で、不揮発性メモリと呼ばれます。<br />特徴としては電源を切ってもデータが失われないことがあげられ、SDカードやUSBメモリなどのフラッシュメモリに使用されます。
<h3>RAM</h3>
「Random Access Memory」の略で、揮発メモリと呼ばれます。<br />特徴としては電源を切るとデータが失われることがあげられ、主に主記憶装置やキャッシュメモリに使用されます。<br /><br />また、RAMは性質により以下の2つに分けられます。
<ul>
	<li>DRAM</li>
	<li>SRAM</li>
</ul>
<table>
	<thead>
		<tr>
			<th width="50%">DRAM</th>
			<th width="50%">SRAM</th>
		</tr>
	</thead>
	<tbody>
		<tr>
			<td>安価である反面、実行効率は低いメモリです。<br />主に主記憶に使用されます。</td>
			<td>非常に効果ですが、高い実行効率を持つメモリです。<br />主にキャッシュメモリに使用されます。</td>
		</tr>
	</tbody>
</table>
プログラムを実行する際にはまず、補助記憶装置に保存されているプログラムを主記憶に読み込みます。<br />これを1命令文ずつ取り出してCPUが処理します。<br /><br />主記憶から命令文を取り出す際に係るタイムラグによりCPUが効率に使用できないという問題から主記憶よりも高速なキャッシュメモリを間に置くことが多いです。<br /><br />最初にキャッシュメモリにアクセスして存在しなければ、主記憶から命令文を取り出します。<br /><br />必要とするデータがキャッシュメモリに存在する確率を<strong>ヒット率</strong>と呼びます。<br />ヒット率が高いほど、CPUを効率よく使用できます。
<h2>補助記憶装置</h2>
コンピュータに長期間保存するために使用される記憶装置です。<br />主記憶があくまでもプログラム実行時に必要なデータを一時的に保存するのに対して、補助記憶装置はプログラムの実行に関係なく保存するデータを扱います。<br /><br />代表的な補助記憶装置には以下のものがあります。
<ul>
	<li>磁気ディスク</li>
	<li>光ディスク</li>
	<li>半導体メモリ</li>
	<li>光磁気ディスク</li>
	<li>磁気テープ</li>
	<li>HDD</li>
	<li>SSD</li>
</ul>
<h3>フラグメンテーション</h3>
断片化と良くされます。<br />記憶装置上でデータの保存と削除を繰り返すことで使用できない小さな空の領域が発生し、1つのファイルデータがバラバラの領域に保存されることを言います。<br />これにより、記憶装置のアクセス効率が低下するという問題があります。
<h3>デフラグメンテーション</h3>
フラグメンテーションによってバラバラになったデータをまとめなおす作業を言います。
<h2>入出力インタフェース</h2>
インターフェースとは、「何かと何かをつなぐもの」です。<br />人と人が「言葉」というインターフェースを使用してコミュニケーションを図るのに対して、コンピュータ同士は専用のインターフェースを使用してコミュニケーションを図ります。
<h3>転送方法</h3>
