<h3>インターフェースの種類</h3>
ここでは以下のインターフェースを紹介します。
<ul>
	<li>USB</li>
	<li>IEEE1394</li>
	<li>DVI</li>
	<li>HDMI</li>
	<li>Bluetooth</li>
	<li>IrDA</li>
	<li>RFID</li>
	<li>NFC</li>
</ul>
<h4>USB</h4>
「Universal Serial Bus」の略で、最も一般的なインターフェースです。<br />「シリアル転送」型で、最大127台を同時に接続できます。<br /><br />特徴としては、接続するだけで自動的に初期化作業が行われる<strong>プラグアンドプレイ</strong>機能や、PC側の電源を落すことなく抜き差しが可能な<strong>ホットプラグ</strong>機能を有していることが挙げられます。<br /><br />また、接続中に電源を供給する<strong>バスパワー</strong>機能も有しています。
<h4>IEEE1394</h4>
IEEE(アイトリプルイー)というアメリカの電子学会による規格です。<br />主に家電商品で使用されます。<br /><br />USBと同様に「シリアル転送」型で、プラグアンドプレイ機能やホットプラグ機能を有しています。<br /><br />最大63台までを接続可能です。
<h4>DVI</h4>
「Digital Visual Interface」の略で、ディスプレイを接続するためのインターフェースです。<br />無圧縮のデジタル映像データを送信することが可能であり、高画質・高解像度のデータを送信できます。
<h4>HDMI</h4>
「High-Definition Multimedia Interface」の略で、ディスプレイを接続するためのインターフェースです。<br />前述したDVIは映像のみの規格で、音声は別のケーブルで伝送する必要がありました。<br /><br />これに対して、HDMIはが画像データと音声データを一括して送信することができます。
<h4>IrDA</h4>
無線方式のインターフェースです。<br />赤外線を用いて通信を行うため、途中に障害物があると通信が遮断されます。
<h4>Bluetooth</h4>
無線方式のインターフェースです。<br />2.4GHz帯の電波を用いて通信を行います。<br />途中に障害物があっても問題なく通信を行うことができます。
<h4>RFID</h4>
電波を用いてRFタグのデータを非接触で読み書きするシステムです。
<p>引用元は<a href="https://www.denso-wave.com/ja/adcd/fundamental/rfid/rfid/index.html">こちら</a>。</p>
簡単に説明すればバーコードの進化バージョンです。
