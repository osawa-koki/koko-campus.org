<h2>5大装置</h2>
コンピュータは基本的には以下の5つの装置から構成されます。
<ul>
	<li>入力装置</li>
	<li>記憶装置</li>
	<li>制御装置</li>
	<li>演算装置</li>
	<li>出力装置</li>
</ul>
<img src="../pics/5大装置.png" alt="5大装置" />
このうち、制御機能と演算機能はまとめて処理機能と呼ばれ、これはCPU(プロセッサ)と呼ばれる部分が担当します。
