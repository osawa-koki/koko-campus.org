		<h4>管理会計</h4>
		<p>管理会計ってなんだか覚えていますか?</p>
		<p>復習しますね♪<br>管理会計とは企業内部の人(主に経営者・部長)に向けた会計です。<br>ですから、財務諸表(財務会計)が事実を適切に報告する必要があるのに対して、管理会計では経営に際して有益な情報を提供する必要があるんです。「経営に際して有益な情報」ってなんやって思いますよね、、、結論としては企業(経営者)によって異なります。ある企業が欲してるデータは他の企業からしたらいらなかったりと、、、</p>
		<p>ここでは、管理会計の礎となる損益分岐分析について勉強しましょう♪</p>
		<p>利益は簡単に言えば<strong>「収益 - 費用」</strong>です。<br>ですが、ここでは費用を「固定費と変動費」に分解して考えましょう。<br>「利益 = 収益 - (変動費 + 固定費)」になりますね♪</p>
		<p>次に収益・変動費・固定費について簡単に学びましょう♪</p>
		<p>最初に<strong>収益</strong>について、<br>簡単な問題です。<br>1杯1000円のラーメンを30杯販売しました。収益はいくらですか?</p>
		<p>簡単ですね♪答えは30000円です。収益は販売単価 × 販売数量で計算できます。これは「y = ax」の直線になりますね♪</p>
		<img class = "break_even_analysis_img" src = "./pics/revenue.png" alt = "収益直線の画像">
		<p>次に<strong>変動費</strong>について、<br>変動費とは、売れば売るほど費用もかかってくるモノ♪<br>ラーメン一杯つくるのに600円かかるとしますね。<br>10杯販売したら費用はいくらですか?<br>答えは6000円です。変動費もOKですね♪これも「y = ax」の直線になりますね♪</p>
		<img class = "break_even_analysis_img" src = "./pics/var-cost.png" alt = "変動費直線の画像">
		<p>最後に<strong>固定費</strong>について、<br>ラーメンを作るための鍋を3000円で買いました。この鍋でラーメンを10杯料理しました。費用はいくらですか?<br>答えは3000円です。ちなみに15杯作ろうが100杯作ろうが費用は3000円で<strong>固定</strong>されています。これが固定費です。これは「y = b」の横に走る直線になることに注意です♪</p>
		<img class = "break_even_analysis_img" src = "./pics/fixed-cost.png" alt = "固定費直線の画像">
		<p><small>※簡単のため、少し説明を省きました。簿記2級ではもう少し詳しく説明しています。興味があれば是非♪</small></p>
		<p>ではここで変動費と固定費を合算した総費用直線を想像してみましょう♪</p>
		<img class = "break_even_analysis_img" src = "./pics/cost.png" alt = "総費用直線の画像">
		<p>収益は「y = ax」であるのに対して、費用は「y = ax + <strong>b</strong>」になるんです、、、<br>ってことはどこかで費用線と収益線が交わるハズ!!!言い換えれば「費用 = 収益」となる点(利益がゼロになる点)が存在しますよね♪<br>これを求めるのが損益分岐分析です。<br></p>
		<img class = "break_even_analysis_img" src = "./pics/break-even.png" alt = "損益分岐分析の画像">
		<br><br><br>
		<h4>損益分岐分析問題の解き方</h4>
		<p>上で単語については詳しく説明したんですけど、解き方については公式だけを簡単に説明しますね♪</p>
		<p><small>※理由はITパスポート試験ではそこまで高度な理解を求められないからです。詳しく学びたい方は簿記2級以上・基本情報技術者試験以上のページで会いましょう♪</small></p>
